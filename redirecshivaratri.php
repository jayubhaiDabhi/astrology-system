        <p class="dpcontent">Maha Shivaratri is one of the significant festivals dedicated to Lord Shiva. The day has many legends associated with it. According to most of the legends, the day of Maha Shivaratri is associated with Lord Shiva and many cosmic events related to Him took place on the very same day.</p>
