        <p class="dptitle">Maha Shivaratri Origin | Significance</p>
        <p class="dpcontent">There are several legends which are associated with Shivaratri. Few of the popular beliefs behind observing Maha Shivaratri are -</p>
        <ul class="dpcontent">
            <li class="dplist">It is believed that during the creation of the universe, Lord Shiva was incarnated as Lord Rudra during the midnight of Maha Shivaratri with the grace of Lord Brahma.</li>
            <li class="dplist">It is believed that Lord Shiva got married to Goddess Parvati on the very same day. The festival is observed as the convergence of Shiva and Shakti. Hence, for many Shiva devotees, Shivaratri is observed as the marriage anniversary of Lord Shiva and Goddess Parvati.</li>
            <li class="dplist">As per Hinduism, the creation and the destruction of the universe is a cyclic process. When the time comes, Lord Shiva destroys the whole creation with the fire of His third eye while performing the cosmic dance known as Tandava. It is believed that it is the day of Maha Shivaratri when Lord Shiva does the Tandava. Hence, Maha Shivaratri marks the anniversary of cosmic dance performed by Lord Shiva.</li>
            <li class="dplist">It is believed that during the churning of the great ocean the poison also came out of the sea. It had the power to destroy the whole creation. Lord Shiva drank the poison and saved the whole world from annihilation. Hence, Maha Shivaratri is observed as the thanksgiving to Lord Shiva.</li>
            <li class="dplist">The day of Maha Shivaratri is considered the most favorite day of Lord Shiva. Hence devotees worship Lord Shiva and observe a day and night long fast to please and to seek the blessings of Lord Shiva on His favorite day.</li>
        </ul>
        <p class="dptitle">Maha Shivaratri Deity(s)</p>
        <p class="dpcontent">Lord Shiva is the main deity of Maha Shivaratri. Lord Shiva in the form of Lingam is worshipped on the auspicious day of Maha Shivaratri.</p>
        <p class="dptitle">Maha Shivaratri Date and Time</p>
        <p class="dpcontent">As per Purnimanta calendar Maha Shivaratri is observed on -</p>
        <span class="dpred dpcontent">
            Krishna Paksha Chaturdashi (14<sup>th</sup> day) during Phalguna (12<sup>th</sup> month)
        </span>
        <p class="dpcontent">As per Amanta calendar Maha Shivaratri is observed on -</p>
        <span class="dpred dpcontent">
            Krishna Paksha Chaturdashi (14<sup>th</sup> day) during Magha (11<sup>th</sup> month)
        </span>
        <p class="dpcontent">It should be noted that in both calendars the day of Maha Shivaratri is observed on the same day. It is the nomenclature of lunar months which differs in both calendar.</p>
        <p class="dptitle">Maha Shivaratri Festivals List</p>
        <p class="dpcontent">Maha Shivaratri is the day of austerity rather being the day of joy and merriment. Maha Shivaratri is observed for a single day and night.</p>
        <p class="dptitle">Maha Shivaratri Observance</p>
        <ul class="dpcontent">
            <li class="dplist">the day and night long fasting</li>
            <li class="dplist">worshipping Shiva Lingam</li>
            <li class="dplist">Abhishekam i.e. bathing the Shiva Lingam with water, milk and honey</li>
            <li class="dplist">offering Bel Patra i.e. wood-apple leaves to Shiva Lingam</li>
            <li class="dplist">offering white flowers to Shiva Lingam</li>
            <li class="dplist">night long vigil while chanting Shiva Mantra and Stotram</li>
        </ul>
        <p class="dptitle">Maha Shivaratri Dishes</p>
        <ul class="dpcontent">
            <li class="dplist">Bhang and Thandai</li>
        </ul>
        <p class="dptitle">Maha Shivaratri Public Life</p>
        <p class="dpcontent">Maha Shivaratri is not a compulsory Gazetted Holiday in India. However, in most North Indian states one day holiday is observed on the day of Maha Shivaratri. In other states, most of the government offices and public places work as usual. In small towns where Shiva procession known as Shiva Ji Ki Barat is taken out of streets traffic might be affected or diverted to alternate routes.</p>
        <p class="dpcontent">However, one can expect serpentine queue of devotees at most Shiva temples on the day of Maha Shivaratri. Devotees waiting in the queue for their turn to offer Abhishekam is the common sight irrespective of size of the temple and city.</p>
        <p class="dpcontent">In the evening most Shiva temples offer especial Darshan of Shiva Lingam and thousands of devotees throng to Shiva temples to seek blessings of Lord Shiva.</p>
