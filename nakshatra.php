            <h2 class="dptitle">About Nakshatra</h2>
            <p class="dpcontent">Nakshatra (Sanskrit: नक्षत्रम्, romanized: Nakṣatram) is the term for Lunar mansion in Hindu astrology and Buddhist astrology. A nakshatra is one of 27 (sometimes also 28) sectors along the ecliptic. Their names are related to a prominent star or asterisms in or near the respective sectors.</p>
            <p class="dpcontent">Vedic astrology divides zodiac into 27 parts and each part is recognized with unique name. In Devanagari Nakshatra is written as नक्षत्र.</p>
