        <h2 class="dptitle">Planetary Aspects Explained</h2>
        <p class="dpcontent">Aspect Explained with Example
            Namaste. Let us learn aspects today. Aspect
            is called "Drishti" in Sanskrit. It means
            "to look at" or "to see". Like conjunction,
            planets affect each other via aspect too.
            |Interpretation of conjunction and aspect is
            more or less same. But the way we calculate
            aspect is different from the way we calculate
            conjunction.</p>
        <p class="dpcontent">Every planet aspects or sees certain places
            from its place. Not only it aspects the places
            but also the planets situated there. All the
            planets aspect seventh from the place
            they are posited. Mars, Saturn and Jupiter
            have additional aspects. Mars, apart from
            standard 7th, aspects fourth and eighth
            place. Similarly, Jupiter also has fifth and
            ninth aspects and Saturn has third and tenth
            aspect too. You can see aspect of each
            planet on the blackboard.</p>
        <p class="dpcontent">PlanetAspectSun7Moon7Mercury7Venus7Mars4, 7, 8Jupiter5, 7, 9Saturn3, 7,
            10
            Rahu and Ketu do not have any aspect.
            In our chart, Sun is situated in 7th house.
            We know that all planets aspect 7th from
            its place. When we start counting from
            Sun, this is first, this is second, this is third,
            fourth, fifth, sixth and seven. Always keep
            in mind that in North Indian Chart, we count
            anti-clock-wise. So this place which comes
            but to be ascendant or first house is the
            house aspected by Sun. There is no planet
            in first house and hence Sun doesn't aspect
            any planet there.</p>
        <p class="dpcontent">Now let us take example of Jupiter. We know
            that apart from standard 7th, Jupiter also
            aspects 5th and 9th from its place. If we
            count 5 from the place of Jupiter, we reach
            to 3rd house where 1 is written. If we count
            7, we reach to 5th house where Venus and
            Rahu are situated. If we count 9, we will
            reach to 7th house where Sun, Saturn and
            Mercury are situated. So we will say that
            Jupiter aspects Venus, Rahu, Saturn, Sun
            and Mercury in this chart.
        </p>
        <p class="dpcontent">Interpretation of aspects is similar to
            interpretation of conjunction. Always keep in
            mind that nature of planet is very important
            when it comes to interpreting aspects.
            Aspects of natural benefics like Jupiter,
            Venus etc. improves the matter and the
            aspects of natural malefics like Saturn and
            Mars harms matter.</p>

