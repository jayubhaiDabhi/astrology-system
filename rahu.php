            <h2 class="dptitle">About Rahu Kaal</h2>
            <p class="dpcontent">According to Vedic astrology Rahu is an inauspicious planet. During transition of planets the time under the influence of Rahu should be avoided to do any auspicious work. Doing Puja, Hawan or Yagya (यज्ञ) to propitiate auspicious planets during this time is interfered by Rahu due to its malefic nature. If one does Puja, Hawan or Yagya during Rahu Kaal then desired results are not achieved. Hence it is important to consider Rahu Kaal before starting any new work. By doing so, possibility of achieving desired results is increased.</p>
            <p class="dpcontent">However any work related to Rahu gives good results during this period. Hawan, Yagya, etc. to propitiate Rahu can be done during this time.</p>
            <p class="dpcontent">People, especially in South India, give utmost importance to Rahu Kaal. Auspicious activities like marriage rituals, engagement, Graha Pravesh, any purchase of stocks, shares, gold, home, car and starting new business or trade are avoided during this time. Rahu Kaal is considered only for undertaking any new work and already started work can be continued during Rahu Kaal.</p>
            <p class="dpcontent">Rahu Kaal, which is also spelled as Rahu Kala, Rahu Kal, Rahu Kalam and Rahu Kalaam, is a certain amount of time every day which lasts approximately for one and half hour. Rahu Kaal is one of the eight segments of the day between sunrise and sunset. Eight segments of the day are calculated by taking the total time between sunrise and sunset at a given place and then dividing this time duration by eight.</p>
            <p class="dpcontent">Rahu Kaal timings and duration are not same for any two locations due to difference in local timings of sunrise and sunset. Even for one location Rahu Kaal timing and duration are not same for all days as sunrise and sunset timings keep changing throughout the year. In other words Rahu Kaal varies from place to place and from day to day. Hence Rahu Kaal should be looked at for each day.</p>
            <p class="dpcontent">After sunrise first period (out of eight division of time between sunrise and sunset) which approximately lasts for one and half hour is always auspicious as this duration of the day is always free from malefic effects of Rahu. On Monday Rahu Kaal falls on the 2nd period, Saturday on the 3rd period, Friday on the 4th period, Wednesday on the 5th period, Thursday on the 6th period, Tuesday on the 7th period and Sunday on the 8th period.</p>
            <p class="dpcontent">Few people consider Rahu Kaal for night duration also which is less popular as most important and auspicious work are started during day time. However Rahu Kaal during night can also be calculated by dividing duration between sunset and next day sunrise by eight. According to some Rahu Kaal on Tuesday, Friday and Sunday has more malefic effects than those of other weekdays.</p>
