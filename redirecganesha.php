        <p class="dpcontent">Ganesha Chaturthi is also known as Siddhi Vinayaka Chaturthi and Ganesha Chauth. It is considered the most important day to worship Lord Ganesha. Lord Ganesha is considered the God of wisdom and remover of all obstacles. He is worshipped first among all Gods and before starting any Puja or ritual.</p>
        <p class="dptitle">Ganesha Chaturthi Origin | Significance</p>
        <p class="dpcontent">Ganesha Chaturthi is celebrated as the birth anniversary of Lord Ganesha. Although there are contradictory views on the birth anniversary of Lord Ganesha, most people celebrate it on Shukla Chaturthi during Bhadrapada lunar month and the day is popularly known as Ganesha Chaturthi.</p>
        <p class="dpcontent">As per Ganesha Purana and Skanda Purana, Lord Ganesha was born on Shukla Chaturthi during Bhadrapada month. However, as per Shiva Dharma Lord Ganesha was born on Krishna Chaturthi during Magha month.</p>
        <p class="dpcontent">In North India, Magha Krishna Chaturthi is observed as Sakat Chauth. Although, Sakat Chauth is not observed as birth anniversary of Lord Ganesha, it is considered a significant day to worship Lord Ganesha. In Maharashtra and Konkan regions, Shukla Chaturthi during Magha month is observed as Ganesha Jayanti and it is celebrated as birth anniversary of Lord Ganesha apart from Ganesha Chaturthi. The reason to observe Ganesha Jayanti during Shukla Paksha and not during Krishna Paksha as mentioned in Shiva Dharma is not clear.</p>
