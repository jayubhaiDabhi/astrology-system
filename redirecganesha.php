        <p class="dptitle">Ganesha Chaturthi Public Life</p>
        <p class=dpcontent>Ganesha Chaturthi is an optional gazetted holiday in India. Hence, most government offices, businesses, schools and colleges remain open during Ganesha Chaturthi.</p>
        <p class=dpcontent>Anant Chaturdashi which falls after 10 days of Ganesha Chaturthi is considered significant to bid farewell to Lord Ganesha. Anant Chaturdashi is the culmination day of Ganesha Chaturthi celebrations when the idols of Lord Ganesha are offered to water body through Visarjan and immersion.</p>
        <p class=dpcontent>Compare to Ganesha Chaturthi, public life is significantly affected on the day of Ananta Chaturdashi as the grand street processions are taken out with much fun and frolic. The roads and streets are blocked with devotees of Lord Ganesha. The movement of vehicular traffic on some significant roads is blocked due to several Ganesha processions.</p>
        <p class=dpcontent>In some states, the day of Anant Chaturdashi is declared as dry day to control and minimize untoward incidents. The authorities keep vigil to prevent any communal tension getting escalated during Ganesha procession. On a dry day all liqueur shops remain closed and all hotels and restaurants except five stars are prohibited to serve alcohol by local authorities.</p>
        <p class=dpcontent>Anant Chaturdashi is listed under restricted government holidays. Hence, individuals can take a day off on the day of Ganesha Visarjan. The system of restricted holidays in India gives individuals the flexibility to take time off to celebrate those festivals which are important to their region and religion.</p>
