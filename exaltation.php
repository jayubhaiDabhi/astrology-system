        <p class="dpcontent">There are some interesting points in this
            table. First, the exalted and the debilitated
            signs for a planet are seventh from each
            other, e.g. Sun is exalted in Aries, the first
            sign of zodiac, and is debilitated in Libra,
            seventh sign of zodiac. Second, Venus
            and Mercury have opposite exaltation
            and debilitation signs. Same is true for
            Saturn-Sun & Jupiter-Mars. Third, exaltation
            and debilitation sign for Rahu and Ketu are
            not that important.</p>
        <p class="dpcontent">A planet is most powerful in its exalted sign.
            In its own sign, it has power of second grade.
            In its friendly sign, it has power of third
            grade. In neutral sign, it has power of fourth
            grade. In enemy sign, it has power of fifth
            grade. In debilitated sign, a planet has the
            power of sixth grade; i.e. a planet is weakest
            in it.</p>
        <p class="dpcontent">While analyzing a birth-chart, you should
            note down those planets that are in
            exaltation and in debilitation. The planets
            posited in exalted signs are able to give
            their results. If a planet is in debilitated or
            enemy sign, it becomes weak and cannot
            give its results. When we say "results", it
            means planets' own significations and the
            significations of the houses it owns</p>

