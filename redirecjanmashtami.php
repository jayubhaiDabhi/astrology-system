        <p class="dpcontent">Krishna Janmashtami is observed as the birth anniversary of Lord Krishna. Krishna Janmashtami is also known as Gokulashtami, Shrikrishna Jayanti and just Janmashtami. In Gujarat Krishna Janmashtami is also known as Satam Atham and in South India especially in Kerala Krishna Janmashtami is observed as Ashtami Rohini.</p>
