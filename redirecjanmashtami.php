        <p class="dpcontent">Janmashtami is the annual celebration of the birth anniversary of Lord Krishna and it is celebrated throughout India.</p>
        <p class="dptitle">Janmashtami Origin | Significance</p>
        <p class="dpcontent">Janmashtami is getting observed since the birth era of Lord Krishna. Janmashtami is the 5200+ years old ritualistic celebration of the birth anniversary of Lord Krishna. As per Vedic time chronology, in 2023 it would be the 5250th birth anniversary of Lord Krishna.</p>
        <p class="dpcontent">Krishna Janmashtami is significant event in most Krishna temples. The cities and towns which are related to Lord Krishna, Janmashtami is a household celebration. In the historic cities, Mathura, Vrindavan and Dwarka Krishna Janmashtami is celebrated as the birth anniversary of a family member.</p>
        <p class="dptitle">Janmashtami Deity(s)</p>
        <p class="dpcontent">The main deity which is worshipped during Janmashtami is Lord Krishna. As Janmashtami is the birth anniversary of Lord Krishna, the toddler form of Lord Krishna, which are known as Bal Gopal and Laddu Gopal, are worshipped on the day of Krishna Janmashtami.</p>
        <p class="dpcontent">Apart from young Lord Krishna, the biological parents of Lord Krishna i.e. Vasudeva and Devaki, the foster parents of Lord Krishna i.e. Nanda and Yashoda and the siblings of Lord Krishna i.e. Balabhadra (Lord Balarama) and Subhadra are also worshipped during Janmashtami Puja.</p>
        <p class="dptitle">Janmashtami Date and Time</p>
        <p class="dpcontent">As per Amanta Hindu Calendar</p>
        <span class="dpred dpcontent">
            Krishna Paksha Ashtami (23<sup>th</sup> day) of Shravana (5<sup>th</sup> month)
        </span>
        <p class="dpcontent">As per Purnimanta Calendar</p>
        <span class="dpred dpcontent">
            Krishna Paksha Ashtami (8<sup>th</sup> day) of Bhadrapada (6<sup> month)
        </span>
        <p class="dpcontent">However in both calendars Krishna Janmashtami is observed on the same day.</p>
        <p class="dptitle">Janmashtami Festivals List</p>
        <p class="dpcontent">At most places, Janmashtami celebrations last for 2 days.</p>
        <ul class="dpcontent">
            <li class="dplist">Day 1 - Krishna Janmashtami</li>
            <li class="dplist">Day 2 - Dahi Handi</li>
        </ul>
        <p class="dptitle">Janmashtami Observance</p>
        <ul class="dpcontent">
            <li class="dplist">a day long fast</li>
            <li class="dplist">worshipping Bal Krishna during midnight</li>
            <li class="dplist">visiting Krishna temple</li>
            <li class="dplist">cooking sweet dishes specially made of milk product</li>
        </ul>
        <p class="dptitle">Janmashtami Regional Variance</p>
        <p class="dpcontent">Most people in India celebrate Krishna Janmashtami based on Lunar Calendar but few temples and some regions in South India observe Krishna Janmashtami based on Solar Calendar. In most years these dates don't differ by one or two days but in some years Janmashtami date based on Lunar Calendar and Janmashtami date based on Solar Calendar might differ up to one month.</p>
        <p class="dpcontent">In Kerala, Tamil Nadu and in some parts of Karnataka, Krishna Janmashtami is popularly known as Ashtami Rohini and is observed based on Solar Calendar.</p>
        <p class="dptitle">Janmashtami Public Life</p>
        <p class="dpcontent">Krishna Janmashtami is an optional gazetted holiday in India. However, most government offices, schools and colleges keep one day off during Krishna Janmashtami.</p>
