            <p class="dpcontent">The Solstice is an astronomical event that happens twice, once in summer and once in winter, each year when the Sun reaches its highest position in the sky as seen from the North or South Pole. During Solstices the tilt of the axis of the Earth (with respect to the Sun) is the maximum at 23° 26'.</p>
            <p class="dpcontent">Solstices occur on 20th or 21st June and 21st or 22nd December each year. During summer the day of the solstice is the longest day of the year and during winter the day of the solstice is the shortest day of the year.</p>
            <p class="dpcontent">During June it is Summer Solstice in the Northern hemisphere and Winter Solstice in the Southern hemisphere. In other words, on June Solstice it is summer time in the UK, the USA, Canada, Russia, India, and China and it is the longest day of the year while it is winter time in Australia, Argentina, Chile, New Zealand and South Africa and it is the shortest day of the year.</p>
            <p class="dpcontent">Similarly, during December it is Winter Solstice in the Northern hemisphere and Summer Solstice in the Southern hemisphere. In other words, during December Solstice it is winter time in the UK, the USA, Canada, Russia, India and China and it is the shortest day of the year while it is summer time in Australia, Argentina, Chile, New Zealand and South Africa and it is the longest day of the year.</p>
            <p class="dpcontent">To avoid any confusion Solstices are preferably referred to as June Solstice (Northern Solstice) and December Solstice (Southern Solstice). Winter Solstice is also known as Hibernal Solstice.</p>
