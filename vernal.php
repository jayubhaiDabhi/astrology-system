            <p class="dpcontent">An Equinox is an astronomical event that happens twice, once in spring and once in autumn, each year when the tilt of the Earth's axis is inclined neither away from nor towards the Sun. During Equinoxes the tilt of the Earth (with respect to the Sun) is 0° and because of its duration of the day and the night are almost equal on Equinox day i.e. 12 hours.</p>
            <p class="dpcontent">Equinoxes occur on 20th or 21st March and 22nd or 23rd September each year and both days have equal length of the day and the night.</p>
            <p class="dpcontent">During March it is springtime in the Northern hemisphere and March Equinox is called Vernal Equinox and Spring Equinox while it is an autumn time in the Southern hemisphere and March Equinox is called Autumnal Equinox and Fall Equinox. In other words, on March Equinox it is springtime in the UK, the USA, Canada, Russia, India and China while it is autumn time in Australia, Argentina, Chile, New Zealand and South Africa.</p>
            <p class="dpcontent">Similarly, during September it is autumn time in the Northern hemisphere and September Equinox is called Autumnal Equinox and Fall Equinox while it is springtime in the Southern hemisphere and September Equinox is called Vernal Equinox and Spring Equinox. In other words, on September Equinox it is the autumn time in the UK, the USA, Canada, Russia, India and China while it is springtime in Australia, Argentina, Chile, New Zealand and South Africa.</p>
