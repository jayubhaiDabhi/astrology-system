        <p class="dptitle">Makar Sankranti Origin | Significance</p>
        <p class="dpcontent">Makar Sankranti is a significant day as the Sun enters Makar Rashi (i.e. Capricorn zodiac) as per Vedic astrology. In Hinduism the Sun is worshipped and known as Surya Deva who nourishes all living beings on the Earth. Although all twelve days in Hindu calendar when Surya Deva transits a Rashi are considered significant for worshipping Surya Deva, taking religious bath in holy water bodies and performing charity activities but the day when Surya Deva starts moving into Makar Rashi is considered the most auspicious day of the year to worship Lord Surya.</p>
        <p class="dpcontent">Many people wrongly assume and observe Makar Sankranti as the day of Uttarayana. Makar Sankranti and Uttarayana are two separate astronomical as well as religious events. However thousands years before (precisely in year 285 C.E. as per Lahiri Ayanamsha) the day of Makar Sankranti was coinciding with the day of Uttarayana. The Uttarayana is combination of Uttara and Ayana which mean north and six months duration respectively. Hence, by the definition of Uttarayana, it occurs on the day of Winter Solstice.</p>
        <p class="dpcontent">Makar Sankranti is significant due to the transit of Surya in Makar Rashi and Uttarayana is significant due to Surya beginning Northern journey (i.e. start moving into Northern hemisphere) after completing His six months Southern journey. In modern India, people have stopped observing Winter Solstice for any religious activities however Bhishma Pitamah chose Uttarayana i.e. Winter Solstice to leave his body even the day of Uttarayana was not coinciding with Makar Sankranti during Mahabharata era. Hence the day of Winter Solstice is also religiously significant to worship Surya Deva as per Vedic astrology.</p>
        <p class="dpcontent">Makar Sankranti is a major harvest festival is also a wrong assumption. The day of Makar Sankranti continuously drifts away from winter solstice. In year 1600, Makar Sankranti fell on January 9, and in the year 2600, Makar Sankranti would fall on January 23. After 5000 years of 2015, i.e. in the year 7015 Makar Sankranti would be observed on March 23, much after winter season in India and Vernal Equinox. It shows that no way is Sankranti or any other Hindu festivals are linked to seasons. However in current time it happens to be harvest season (at least in some parts of India) at the time of Makar Sankranti and it adds zest to the Sankranti festivities.</p>
        <p class="dptitle">Sankranti Deity(s)</p>
        <p class="dpcontent">The Sun God as Surya Deva is worshipped during Sankranti. In South India, on the next day of Sankranti Lord Krishna is also worshipped. According to legends famous in south India, Lord Krishna lifted Govardhan on the next day of Makar Sankranti.</p>
        <p class="dpcontent">Apart from deities, live stocks and cattle e.g. bulls, cows and oxen are worshipped during Sankranti.</p>
        <p class="dptitle">Sankranti Date and Time</p>
        <p class="dpcontent">The day of Makar Sankranti is decided as per Hindu solar calendar. Makar Sankranti is observed when the Sun transits from Dhanu Rashi to Makar Rashi and it falls on the first day of the tenth solar month in most Hindu calendars.</p>
        <p class="dpcontent">Currently the day of Sankranti falls either on January 14 or January 15 on Gregorian calendar. If Sankranti moment occurs before sunset then it is observed on the same day otherwise it is observed on the next day.</p>
