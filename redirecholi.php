        <h2 class="dptitle">Holi Origin | Holi Significance</h2>
        <p class="dpcontent">Holika Puja and Holika Dahan is a Vedic ritual and getting observed since ages. Religiously, it is very significant day as Prahlada the ardent devotee of Lord Vishnu was saved while his aunt Holika was burnt in huge bonfire meant to immolate Prahlada. Hence, every-year Holika is burnt as a symbol of all evils in the society. However, in modern India, Holi is considered the festival of colors, joy and merriment.</p>
        <h2 class="dptitle">Holi Deity</h2>
        <p class="dpcontent">The demoness Holika, the sister of demon Hiranyakashipu, and Prahlada, the devotee of Lord Vishnu, are the main deities of Holi.</p>
        <p class="dpcontent">The demoness Holika getting worshipped along with devotee Prahlada is one of the mysterious rituals of Hinduism. Please refer why Holika is worshipped? to understand the mystery of worshipping demoness Holika who tried to kill child devotee of Lord Vishnu.</p>
        <h2 class="dptitle">Holi Date and Time</h2>
        <p class="dpcontent">Holi is celebrated as per Hindu lunar calendar. The day of Holika Dahan coincides with the full moon day during the Hindu month of Phalguna which is popularly known as Phalguna Purnima. The next day of Holika Dahan is observed as Holi.</p>
