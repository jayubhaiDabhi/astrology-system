                <h2 class="dptitle1">॥ दोहा ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय गणेश गिरिजा सुवन,</span>
                        <span class="dprow">मंगल करण कृपाल।</span>
                    </p>
                    <p>
                        <span class="dprow">दीनन के दुःख दूर करि,</span>
                        <span class="dprow">कीजै नाथ निहाल॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय जय श्री शनिदेव प्रभु,</span>
                        <span class="dprow">सुनहु विनय महाराज।</span>
                    </p>
                    <p>
                        <span class="dprow">करहु कृपा हे रवि तनय,</span>
                        <span class="dprow">राखहु जन की लाज॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ चौपाई ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जयति जयति शनिदेव दयाला।</span>
                        <span class="dprow">करत सदा भक्तन प्रतिपाला॥</span>
                    </p>
                    <p>
                        <span class="dprow">चारि भुजा, तनु श्याम विराजै।</span>
                        <span class="dprow">माथे रतन मुकुट छवि छाजै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">परम विशाल मनोहर भाला।</span>
                        <span class="dprow">टेढ़ी दृष्टि भृकुटि विकराला॥</span>
                    </p>
                    <p>
                        <span class="dprow">कुण्डल श्रवण चमाचम चमके।</span>
                        <span class="dprow">हिये माल मुक्तन मणि दमके॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कर में गदा त्रिशूल कुठारा।</span>
                        <span class="dprow">पल बिच करैं अरिहिं संहारा॥</span>
                    </p>
                    <p>
                        <span class="dprow">पिंगल, कृष्णों, छाया, नन्दन।</span>
                        <span class="dprow">यम, कोणस्थ, रौद्र, दुःख भंजन॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सौरी, मन्द, शनि, दशनामा।</span>
                        <span class="dprow">भानु पुत्र पूजहिं सब कामा॥</span>
                    </p>
                    <p>
                        <span class="dprow">जा पर प्रभु प्रसन्न है जाहीं।</span>
                        <span class="dprow">रंकहुं राव करैं क्षण माहीं॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पर्वतहू तृण होई निहारत।</span>
                        <span class="dprow">तृणहू को पर्वत करि डारत॥</span>
                    </p>
                    <p>
                        <span class="dprow">राज मिलत वन रामहिं दीन्हो।</span>
                        <span class="dprow">कैकेइहुं की मति हरि लीन्हो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बनहूं में मृग कपट दिखाई।</span>
                        <span class="dprow">मातु जानकी गयी चुराई॥</span>
                    </p>
                    <p>
                        <span class="dprow">लखनहिं शक्ति विकल करिडारा।</span>
                        <span class="dprow">मचिगा दल में हाहाकारा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">रावण की गति मति बौराई।</span>
                        <span class="dprow">रामचन्द्र सों बैर बढ़ाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">दियो कीट करि कंचन लंका।</span>
                        <span class="dprow">बजि बजरंग बीर की डंका॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">नृप विक्रम पर तुहि पगु धारा।</span>
                        <span class="dprow">चित्र मयूर निगलि गै हारा॥</span>
                    </p>
                    <p>
                        <span class="dprow">हार नौलाखा लाग्यो चोरी।</span>
                        <span class="dprow">हाथ पैर डरवायो तोरी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भारी दशा निकृष्ट दिखायो।</span>
                        <span class="dprow">तेलिहिं घर कोल्हू चलवायो॥</span>
                    </p>
                    <p>
                        <span class="dprow">विनय राग दीपक महँ कीन्हों।</span>
                        <span class="dprow">तब प्रसन्न प्रभु है सुख दीन्हों॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">हरिश्चन्द्र नृप नारि बिकानी।</span>
                        <span class="dprow">आपहुँ भरे डोम घर पानी॥</span>
                    </p>
                    <p>
                        <span class="dprow">तैसे नल पर दशा सिरानी।</span>
                        <span class="dprow">भूँजी-मीन कूद गयी पानी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">श्री शंकरहि गहयो जब जाई।</span>
                        <span class="dprow">पार्वती को सती कराई॥</span>
                    </p>
                    <p>
                        <span class="dprow">तनिक विलोकत ही करि रीसा।</span>
                        <span class="dprow">नभ उड़ि गयो गौरिसुत सीसा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पाण्डव पर भै दशा तुम्हारी।</span>
                        <span class="dprow">बची द्रोपदी होति उघारी॥</span>
                    </p>
                    <p>
                        <span class="dprow">कौरव के भी गति मति मारयो।</span>
                        <span class="dprow">युद्ध महाभारत करि डारयो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">रवि कहं मुख महं धरि तत्काला।</span>
                        <span class="dprow">लेकर कूदि परयो पाताला॥</span>
                    </p>
                    <p>
                        <span class="dprow">शेष देव-लखि विनती लाई।</span>
                        <span class="dprow">रवि को मुख ते दियो छुड़ाई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">वाहन प्रभु के सात सुजाना।</span>
                        <span class="dprow">हय दिग्ज गर्दभ मृग स्वाना॥</span>
                    </p>
                    <p>
                        <span class="dprow">जम्बुक सिंह आदि नख धारी।</span>
                        <span class="dprow">सो फल ज्योतिष कहत पुकारी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">गज वाहन लक्ष्मी गृह आवैं।</span>
                        <span class="dprow">हय ते सुख सम्पत्ति उपजावै॥</span>
                    </p>
                    <p>
                        <span class="dprow">गर्दभ हानि करै बहु काजा।</span>
                        <span class="dprow">सिंह सिद्धकर राज समाजा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जम्बुक बुद्धि नष्ट कर डारै।</span>
                        <span class="dprow">मृग दे कष्ट प्राण संहारै॥</span>
                    </p>
                    <p>
                        <span class="dprow">जब आवहिं प्रभु स्वान सवारी।</span>
                        <span class="dprow">चोरी आदि होय डर भारी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तैसहि चारि चरण यह नामा।</span>
                        <span class="dprow">स्वर्ण लौह चाँजी अरु तामा॥</span>
                    </p>
                    <p>
                        <span class="dprow">लौह चरण पर जब प्रभु आवैं।</span>
                        <span class="dprow">धन जन सम्पत्ति नष्ट करावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">समता ताम्र रजत शुभकारी।</span>
                        <span class="dprow">स्वर्ण सर्वसुख मंगल कारी॥</span>
                    </p>
                    <p>
                        <span class="dprow">जो यह शनि चरित्र नित गावै।</span>
                        <span class="dprow">कबहुं न दशा निकृष्ट सतावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अदभुत नाथ दिखावैं लीला।</span>
                        <span class="dprow">करैं शत्रु के नशि बलि ढीला॥</span>
                    </p>
                    <p>
                        <span class="dprow">जो पण्डित सुयोग्य बुलवाई।</span>
                        <span class="dprow">विधिवत शनि ग्रह शान्ति कराई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पीपल जल शनि दिवस चढ़ावत।</span>
                        <span class="dprow">दीप दान दै बहु सुख पावत॥</span>
                    </p>
                    <p>
                        <span class="dprow">कहत राम सुन्दर प्रभु दासा।</span>
                        <span class="dprow">शनि सुमिरत सुख होत प्रकाशा॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ दोहा ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पाठ शनिश्चर देव को,</span>
                        <span class="dprow">कीन्हों विमल तैयार।</span>
                    </p>
                    <p>
                        <span class="dprow">करत पाठ चालीस दिन,</span>
                        <span class="dprow">हो भवसागर पार॥</span>
                    </p>
                    <p></p>
                </div>

