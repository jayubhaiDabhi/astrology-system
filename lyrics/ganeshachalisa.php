                <h2 class="dptitle1">॥ दोहा ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय गणपति सदगुण सदन,</span>
                        <span class="dprow">कविवर बदन कृपाल।</span>
                    </p>
                    <p>
                        <span class="dprow">विघ्न हरण मंगल करण,</span>
                        <span class="dprow">जय जय गिरिजालाल॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ चौपाई ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय जय जय गणपति गणराजू।</span>
                        <span class="dprow">मंगल भरण करण शुभः काजू॥</span>
                    </p>
                    <p>
                        <span class="dprow">जै गजबदन सदन सुखदाता।</span>
                        <span class="dprow">विश्व विनायका बुद्धि विधाता॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">वक्र तुण्ड शुची शुण्ड सुहावना।</span>
                        <span class="dprow">तिलक त्रिपुण्ड भाल मन भावन॥</span>
                    </p>
                    <p>
                        <span class="dprow">राजत मणि मुक्तन उर माला।</span>
                        <span class="dprow">स्वर्ण मुकुट शिर नयन विशाला॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पुस्तक पाणि कुठार त्रिशूलं।</span>
                        <span class="dprow">मोदक भोग सुगन्धित फूलं॥</span>
                    </p>
                    <p>
                        <span class="dprow">सुन्दर पीताम्बर तन साजित।</span>
                        <span class="dprow">चरण पादुका मुनि मन राजित॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">धनि शिव सुवन षडानन भ्राता।</span>
                        <span class="dprow">गौरी लालन विश्व-विख्याता॥</span>
                    </p>
                    <p>
                        <span class="dprow">ऋद्धि-सिद्धि तव चंवर सुधारे।</span>
                        <span class="dprow">मुषक वाहन सोहत द्वारे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कहौ जन्म शुभ कथा तुम्हारी।</span>
                        <span class="dprow">अति शुची पावन मंगलकारी॥</span>
                    </p>
                    <p>
                        <span class="dprow">एक समय गिरिराज कुमारी।</span>
                        <span class="dprow">पुत्र हेतु तप कीन्हा भारी॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भयो यज्ञ जब पूर्ण अनूपा।</span>
                        <span class="dprow">तब पहुंच्यो तुम धरी द्विज रूपा॥</span>
                    </p>
                    <p>
                        <span class="dprow">अतिथि जानी के गौरी सुखारी।</span>
                        <span class="dprow">बहुविधि सेवा करी तुम्हारी॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अति प्रसन्न हवै तुम वर दीन्हा।</span>
                        <span class="dprow">मातु पुत्र हित जो तप कीन्हा॥</span>
                    </p>
                    <p>
                        <span class="dprow">मिलहि पुत्र तुहि, बुद्धि विशाला।</span>
                        <span class="dprow">बिना गर्भ धारण यहि काला॥</span>
                    </p>
                    <p></p>
                </div>
                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">गणनायक गुण ज्ञान निधाना।</span>
                        <span class="dprow">पूजित प्रथम रूप भगवाना॥</span>
                    </p>
                    <p>
                        <span class="dprow">अस कही अन्तर्धान रूप हवै।</span>
                        <span class="dprow">पालना पर बालक स्वरूप हवै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बनि शिशु रुदन जबहिं तुम ठाना।</span>
                        <span class="dprow">लखि मुख सुख नहिं गौरी समाना॥</span>
                    </p>
                    <p>
                        <span class="dprow">सकल मगन, सुखमंगल गावहिं।</span>
                        <span class="dprow">नाभ ते सुरन, सुमन वर्षावहिं॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">शम्भु, उमा, बहुदान लुटावहिं।</span>
                        <span class="dprow">सुर मुनिजन, सुत देखन आवहिं॥</span>
                    </p>
                    <p>
                        <span class="dprow">लखि अति आनन्द मंगल साजा।</span>
                        <span class="dprow">देखन भी आये शनि राजा॥</span>
                    </p>
                    <p></p>
                </div>


                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">निज अवगुण गुनि शनि मन माहीं।</span>
                        <span class="dprow">बालक, देखन चाहत नाहीं॥</span>
                    </p>
                    <p>
                        <span class="dprow">गिरिजा कछु मन भेद बढायो।</span>
                        <span class="dprow">उत्सव मोर, न शनि तुही भायो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कहत लगे शनि, मन सकुचाई।</span>
                        <span class="dprow">का करिहौ, शिशु मोहि दिखाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">नहिं विश्वास, उमा उर भयऊ।</span>
                        <span class="dprow">शनि सों बालक देखन कहयऊ॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पदतहिं शनि दृग कोण प्रकाशा।</span>
                        <span class="dprow">बालक सिर उड़ि गयो अकाशा॥</span>
                    </p>
                    <p>
                        <span class="dprow">गिरिजा गिरी विकल हवै धरणी।</span>
                        <span class="dprow">सो दुःख दशा गयो नहीं वरणी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">हाहाकार मच्यौ कैलाशा।</span>
                        <span class="dprow">शनि कीन्हों लखि सुत को नाशा॥</span>
                    </p>
                    <p>
                        <span class="dprow">तुरत गरुड़ चढ़ि विष्णु सिधायो।</span>
                        <span class="dprow">काटी चक्र सो गज सिर लाये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बालक के धड़ ऊपर धारयो।</span>
                        <span class="dprow">प्राण मन्त्र पढ़ि शंकर डारयो॥</span>
                    </p>
                    <p>
                        <span class="dprow">नाम गणेश शम्भु तब कीन्हे।</span>
                        <span class="dprow">प्रथम पूज्य बुद्धि निधि, वर दीन्हे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बुद्धि परीक्षा जब शिव कीन्हा।</span>
                        <span class="dprow">पृथ्वी कर प्रदक्षिणा लीन्हा॥</span>
                    </p>
                    <p>
                        <span class="dprow">चले षडानन, भरमि भुलाई।</span>
                        <span class="dprow">रचे बैठ तुम बुद्धि उपाई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">चरण मातु-पितु के धर लीन्हें।</span>
                        <span class="dprow">तिनके सात प्रदक्षिण कीन्हें॥</span>
                    </p>
                    <p>
                        <span class="dprow">धनि गणेश कही शिव हिये हरषे।</span>
                        <span class="dprow">नभ ते सुरन सुमन बहु बरसे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तुम्हरी महिमा बुद्धि बड़ाई।</span>
                        <span class="dprow">शेष सहसमुख सके न गाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">मैं मतिहीन मलीन दुखारी।</span>
                        <span class="dprow">करहूं कौन विधि विनय तुम्हारी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भजत रामसुन्दर प्रभुदासा।</span>
                        <span class="dprow">जग प्रयाग, ककरा, दुर्वासा॥</span>
                    </p>
                    <p>
                        <span class="dprow">अब प्रभु दया दीना पर कीजै।</span>
                        <span class="dprow">अपनी शक्ति भक्ति कुछ दीजै॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ दोहा ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">श्री गणेश यह चालीसा,</span>
                        <span class="dprow">पाठ करै कर ध्यान।</span>
                    </p>
                    <p>
                        <span class="dprow">नित नव मंगल गृह बसै,</span>
                        <span class="dprow">लहे जगत सन्मान॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सम्बन्ध अपने सहस्र दश,</span>
                        <span class="dprow">ऋषि पंचमी दिनेश।</span>
                    </p>
                    <p>
                        <span class="dprow">पूरण चालीसा भयो,</span>
                        <span class="dprow">मंगल मूर्ती गणेश॥</span>
                    </p>
                    <p></p>
                </div>

