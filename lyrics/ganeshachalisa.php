    <p class="para">Ganesha Chalisa is a devotional song to glorify Lord Ganesha. It is a poem written in the Awadhi language. The Ganesha Chalisa has gained enormous popularity among the Hindus. Many of them recite it daily as a prayer.</p>
    <p class="para">There are several Chalisa(s) which are dedicated to Lord Ganapati. We have given one of the popular Chalisa of Lord Ganesha.</p>
