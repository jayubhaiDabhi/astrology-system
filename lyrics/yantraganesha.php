                    </span>
                </p>
            </div>

            <h2 class="title">2. Ganesha Shubh Labh Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ श्रीं गं सौभाग्य गणपतये।<br>
                        वर्वर्द सर्वजन्म में वषमान्य नमः॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Shreem Gam Saubhagya Ganpataye।<br>
                        Varvarda Sarvajanma Mein Vashamanya Namah॥
                    </span>
                </p>
            </div>

            <h2 class="title">3. Ganesha Gayatri Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ एकदन्ताय विद्महे, वक्रतुण्डाय धीमहि,<br>
                        तन्नो दन्ति प्रचोदयात्॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Ekadantaya Viddhamahe, Vakratundaya Dhimahi,<br>
                        Tanno Danti Prachodayat॥
                    </span>
                </p>
            </div>

            <h2 class="title">4. Maha Ganapati Moola Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ श्रीं ह्रीं क्लीं ग्लौं गं गणपतये<br>
                        वर वरद सर्वजनं मे वशमानय स्वाहा॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Shrim Hrim Klim Glaum Gam Ganapataye<br>
                        Vara Varada Sarvajanam Me Vashamanaya Svaha॥
                    </span>
                </p>
            </div>

            <h2 class="title">5. Rinharta Ganesha Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ गणेश ऋणं छिन्धि वरेण्यं हुं नमः फट्॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Ganesha Rinam Chhindhi Varenyam Hum Namah Phat॥
                    </span>
                </p>
            </div>

            <h2 class="title">6. Haridra Ganapati Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ हुं गं ग्लौं हरिद्रा गणपतये वर वरद<br>
                        सर्व जन हृदयं स्तम्भय-स्तम्भय स्वाहा॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Hum Gam Glaum Haridra Ganapataye Vara Varada<br>
                        Sarva Jana Hridayam Stambhaya-Stambhaya Svaha॥
                    </span>
                </p>
            </div>

            <h2 class="title">7. Heramba Ganapati Mantra</h2>
            <div>
                <p class="para1">
                    <span>
                        ॐ नमो हेरम्ब मदमोहित<br>
                        मम सङ्कटान् निवारय निवारय स्वाहा॥
                    </span>
                </p>
                <p class="para1 changepara">
                    <span>
                        Om Namo Heramba Madamohita<br>
                        Mama Sankatan Nivaraya Nivaraya Svaha॥
                    </span>
                </p>
            </div>

