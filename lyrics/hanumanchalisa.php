                        <span class="dprow">श्री गुरु चरन सरोज रज,</span>
                        <span class="dprow">निज मनु मुकुर सुधारि।</span>
                    </p>
                    <p>
                        <span class="dprow">बरनउं रघुबर विमल जसु,</span>
                        <span class="dprow">जो दायकु फल चारि॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बुद्धिहीन तनु जानिकै,</span>
                        <span class="dprow">सुमिरौं पवन-कुमार।</span>
                    </p>
                    <p>
                        <span class="dprow">बल बुद्धि विद्या देहु मोहिं,</span>
                        <span class="dprow">हरहु कलेश विकार॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ चौपाई ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय हनुमान ज्ञान गुण सागर।</span>
                        <span class="dprow">जय कपीस तिहुँ लोक उजागर॥</span>
                    </p>
                    <p>
                        <span class="dprow">राम दूत अतुलित बल धामा।</span>
                        <span class="dprow">अंजनि-पुत्र पवनसुत नामा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">महावीर विक्रम बजरंगी।</span>
                        <span class="dprow">कुमति निवार सुमति के संगी॥</span>
                    </p>
                    <p>
                        <span class="dprow">कंचन बरन बिराज सुवेसा।</span>
                        <span class="dprow">कानन कुण्डल कुंचित केसा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">हाथ वज्र औ ध्वजा बिराजै।</span>
                        <span class="dprow">काँधे मूँज जनेऊ साजै॥</span>
                    </p>
                    <p>
                        <span class="dprow">शंकर सुवन केसरीनन्दन।</span>
                        <span class="dprow">तेज प्रताप महा जग वन्दन॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">विद्यावान गुणी अति चातुर।</span>
                        <span class="dprow">राम काज करिबे को आतुर॥</span>
                    </p>
                    <p>
                        <span class="dprow">प्रभु चरित्र सुनिबे को रसिया।</span>
                        <span class="dprow">राम लखन सीता मन बसिया॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सूक्ष्म रुप धरि सियहिं दिखावा।</span>
                        <span class="dprow">विकट रुप धरि लंक जरावा॥</span>
                    </p>
                    <p>
                        <span class="dprow">भीम रुप धरि असुर संहारे।</span>
                        <span class="dprow">रामचन्द्र के काज संवारे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">लाय सजीवन लखन जियाये।</span>
                        <span class="dprow">श्रीरघुवीर हरषि उर लाये॥</span>
                    </p>
                    <p>
                        <span class="dprow">रघुपति कीन्ही बहुत बड़ाई।</span>
                        <span class="dprow">तुम मम प्रिय भरतहि सम भाई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सहस बदन तुम्हरो यश गावैं।</span>
                        <span class="dprow">अस कहि श्री पति कंठ लगावैं॥</span>
                    </p>
                    <p>
                        <span class="dprow">सनकादिक ब्रह्मादि मुनीसा।</span>
                        <span class="dprow">नारद सारद सहित अहीसा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जम कुबेर दिकपाल जहां ते।</span>
                        <span class="dprow">कवि कोबिद कहि सके कहां ते॥</span>
                    </p>
                    <p>
                        <span class="dprow">तुम उपकार सुग्रीवहिं कीन्हा।</span>
                        <span class="dprow">राम मिलाय राज पद दीन्हा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तुम्हरो मन्त्र विभीषन माना।</span>
                        <span class="dprow">लंकेश्वर भये सब जग जाना॥</span>
                    </p>
                    <p>
                        <span class="dprow">जुग सहस्र योजन पर भानू।</span>
                        <span class="dprow">लील्यो ताहि मधुर फ़ल जानू॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">प्रभु मुद्रिका मेलि मुख माहीं।</span>
                        <span class="dprow">जलधि लांघि गए अचरज नाहीं॥</span>
                    </p>
                    <p>
                        <span class="dprow">दुर्गम काज जगत के जेते।</span>
                        <span class="dprow">सुगम अनुग्रह तुम्हरे तेते॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम दुआरे तुम रखवारे।</span>
                        <span class="dprow">होत न आज्ञा बिनु पैसारे॥</span>
                    </p>
                    <p>
                        <span class="dprow">सब सुख लहै तुम्हारी सरना।</span>
                        <span class="dprow">तुम रक्षक काहू को डरना॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">आपन तेज सम्हारो आपै।</span>
                        <span class="dprow">तीनों लोक हांक तें कांपै॥</span>
                    </p>
                    <p>
                        <span class="dprow">भूत पिशाच निकट नहिं आवै।</span>
                        <span class="dprow">महावीर जब नाम सुनावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">नासै रोग हरै सब पीरा।</span>
                        <span class="dprow">जपत निरंतर हनुमत बीरा॥</span>
                    </p>
                    <p>
                        <span class="dprow">संकट ते हनुमान छुड़ावै।</span>
                        <span class="dprow">मन क्रम वचन ध्यान जो लावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सब पर राम तपस्वी राजा।</span>
                        <span class="dprow">तिन के काज सकल तुम साजा॥</span>
                    </p>
                    <p>
                        <span class="dprow">और मनोरथ जो कोई लावै।</span>
                        <span class="dprow">सोइ अमित जीवन फ़ल पावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">चारों जुग परताप तुम्हारा।</span>
                        <span class="dprow">है परसिद्ध जगत उजियारा॥</span>
                    </p>
                    <p>
                        <span class="dprow">साधु सन्त के तुम रखवारे।</span>
                        <span class="dprow">असुर निकन्दन राम दुलारे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अष्ट सिद्धि नवनिधि के दाता।</span>
                        <span class="dprow">अस बर दीन जानकी माता॥</span>
                    </p>
                    <p>
                        <span class="dprow">राम रसायन तुम्हरे पासा।</span>
                        <span class="dprow">सदा रहो रघुपति के दासा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तुम्हरे भजन राम को पावै।</span>
                        <span class="dprow">जनम जनम के दुख बिसरावै॥</span>
                    </p>
                    <p>
                        <span class="dprow">अन्तकाल रघुबर पुर जाई।</span>
                        <span class="dprow">जहाँ जन्म हरि-भक्त कहाई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">और देवता चित्त न धरई।</span>
                        <span class="dprow">हनुमत सेई सर्व सुख करई॥</span>
                    </p>
                    <p>
                        <span class="dprow">संकट कटै मिटै सब पीरा।</span>
                        <span class="dprow">जो सुमिरै हनुमत बलबीरा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय जय जय हनुमान गोसाई।</span>
                        <span class="dprow">कृपा करहु गुरुदेव की नाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">जो शत बार पाठ कर कोई।</span>
                        <span class="dprow">छूटहिं बंदि महा सुख होई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जो यह पढ़ै हनुमान चालीसा।</span>
                        <span class="dprow">होय सिद्धि साखी गौरीसा॥</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसीदास सदा हरि चेरा।</span>
                        <span class="dprow">कीजै नाथ ह्रदय महँ डेरा॥</span>
                    </p>
                    <p></p>
                </div>

