                <h2 class="dptitle1">॥ चौपाई ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">श्री रघुबीर भक्त हितकारी।</span>
                        <span class="dprow">सुनि लीजै प्रभु अरज हमारी॥</span>
                    </p>
                    <p>
                        <span class="dprow">निशि दिन ध्यान धरै जो कोई।</span>
                        <span class="dprow">ता सम भक्त और नहीं होई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">ध्यान धरें शिवजी मन मांही।</span>
                        <span class="dprow">ब्रह्मा, इन्द्र पार नहीं पाहीं॥</span>
                    </p>
                    <p>
                        <span class="dprow">दूत तुम्हार वीर हनुमाना।</span>
                        <span class="dprow">जासु प्रभाव तिहुं पुर जाना॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जय, जय, जय रघुनाथ कृपाला।</span>
                        <span class="dprow">सदा करो संतन प्रतिपाला॥</span>
                    </p>
                    <p>
                        <span class="dprow">तुव भुजदण्ड प्रचण्ड कृपाला।</span>
                        <span class="dprow">रावण मारि सुरन प्रतिपाला॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तुम अनाथ के नाथ गोसाईं।</span>
                        <span class="dprow">दीनन के हो सदा सहाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">ब्रह्मादिक तव पार न पावैं।</span>
                        <span class="dprow">सदा ईश तुम्हरो यश गावैं॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">चारिउ भेद भरत हैं साखी।</span>
                        <span class="dprow">तुम भक्तन की लज्जा राखी॥</span>
                    </p>
                    <p>
                        <span class="dprow">गुण गावत शारद मन माहीं।</span>
                        <span class="dprow">सुरपति ताको पार न पाहिं॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">नाम तुम्हार लेत जो कोई।</span>
                        <span class="dprow">ता सम धन्य और नहीं होई॥</span>
                    </p>
                    <p>
                        <span class="dprow">राम नाम है अपरम्पारा।</span>
                        <span class="dprow">चारिहु वेदन जाहि पुकारा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">गणपति नाम तुम्हारो लीन्हो।</span>
                        <span class="dprow">तिनको प्रथम पूज्य तुम कीन्हो॥</span>
                    </p>
                    <p>
                        <span class="dprow">शेष रटत नित नाम तुम्हारा।</span>
                        <span class="dprow">महि को भार शीश पर धारा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">फूल समान रहत सो भारा।</span>
                        <span class="dprow">पावत कोऊ न तुम्हरो पारा॥</span>
                    </p>
                    <p>
                        <span class="dprow">भरत नाम तुम्हरो उर धारो।</span>
                        <span class="dprow">तासों कबहूं न रण में हारो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">नाम शत्रुहन हृदय प्रकाशा।</span>
                        <span class="dprow">सुमिरत होत शत्रु कर नाशा॥</span>
                    </p>
                    <p>
                        <span class="dprow">लखन तुम्हारे आज्ञाकारी।</span>
                        <span class="dprow">सदा करत सन्तन रखवारी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">ताते रण जीते नहिं कोई।</span>
                        <span class="dprow">युद्ध जुरे यमहूं किन होई॥</span>
                    </p>
                    <p>
                        <span class="dprow">महालक्ष्मी धर अवतारा।</span>
                        <span class="dprow">सब विधि करत पाप को छारा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सीता राम पुनीता गायो।</span>
                        <span class="dprow">भुवनेश्वरी प्रभाव दिखायो॥</span>
                    </p>
                    <p>
                        <span class="dprow">घट सों प्रकट भई सो आई।</span>
                        <span class="dprow">जाको देखत चन्द्र लजाई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जो तुम्हरे नित पांव पलोटत।</span>
                        <span class="dprow">नवो निद्धि चरणन में लोटत॥</span>
                    </p>
                    <p>
                        <span class="dprow">सिद्धि अठारह मंगलकारी।</span>
                        <span class="dprow">सो तुम पर जावै बलिहारी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">औरहु जो अनेक प्रभुताई।</span>
                        <span class="dprow">सो सीतापति तुमहिं बनाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">इच्छा ते कोटिन संसारा।</span>
                        <span class="dprow">रचत न लागत पल की बारा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जो तुम्हरे चरणन चित लावै।</span>
                        <span class="dprow">ताकी मुक्ति अवसि हो जावै॥</span>
                    </p>
                    <p>
                        <span class="dprow">सुनहु राम तुम तात हमारे।</span>
                        <span class="dprow">तुमहिं भरत कुल पूज्य प्रचारे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तुमहिं देव कुल देव हमारे।</span>
                        <span class="dprow">तुम गुरु देव प्राण के प्यारे॥</span>
                    </p>
                    <p>
                        <span class="dprow">जो कुछ हो सो तुमहिं राजा।</span>
                        <span class="dprow">जय जय जय प्रभु राखो लाजा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम आत्मा पोषण हारे।</span>
                        <span class="dprow">जय जय जय दशरथ के प्यारे॥</span>
                    </p>
                    <p>
                        <span class="dprow">जय जय जय प्रभु ज्योति स्वरुपा।</span>
                        <span class="dprow">नर्गुण ब्रहृ अखण्ड अनूपा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सत्य सत्य जय सत्यव्रत स्वामी।</span>
                        <span class="dprow">सत्य सनातन अन्तर्यामी॥</span>
                    </p>
                    <p>
                        <span class="dprow">सत्य भजन तुम्हरो जो गावै।</span>
                        <span class="dprow">सो निश्चय चारों फल पावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सत्य शपथ गौरीपति कीन्हीं।</span>
                        <span class="dprow">तुमने भक्तिहिं सब सिधि दीन्हीं॥</span>
                    </p>
                    <p>
                        <span class="dprow">ज्ञान हृदय दो ज्ञान स्वरुपा।</span>
                        <span class="dprow">नमो नमो जय जगपति भूपा॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">धन्य धन्य तुम धन्य प्रतापा।</span>
                        <span class="dprow">नाम तुम्हार हरत संतापा॥</span>
                    </p>
                    <p>
                        <span class="dprow">सत्य शुद्ध देवन मुख गाया।</span>
                        <span class="dprow">बजी दुन्दुभी शंख बजाया॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सत्य सत्य तुम सत्य सनातन।</span>
                        <span class="dprow">तुम ही हो हमरे तन-मन धन॥</span>
                    </p>
                    <p>
                        <span class="dprow">याको पाठ करे जो कोई।</span>
                        <span class="dprow">ज्ञान प्रकट ताके उर होई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">आवागमन मिटै तिहि केरा।</span>
                        <span class="dprow">सत्य वचन माने शिव मेरा॥</span>
                    </p>
                    <p>
                        <span class="dprow">और आस मन में जो होई।</span>
                        <span class="dprow">मनवांछित फल पावे सोई॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तीनहुं काल ध्यान जो ल्यावै।</span>
                        <span class="dprow">तुलसी दल अरु फूल चढ़ावै॥</span>
                    </p>
                    <p>
                        <span class="dprow">साग पत्र सो भोग लगावै।</span>
                        <span class="dprow">सो नर सकल सिद्धता पावै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अन्त समय रघुबर पुर जाई।</span>
                        <span class="dprow">जहां जन्म हरि भक्त कहाई॥</span>
                    </p>
                    <p>
                        <span class="dprow">श्री हरिदास कहै अरु गावै।</span>
                        <span class="dprow">सो बैकुण्ठ धाम को पावै॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle1">॥ दोहा ॥
                </h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सात दिवस जो नेम कर,</span>
                        <span class="dprow">पाठ करे चित लाय।</span>
                    </p>
                    <p>
                        <span class="dprow">हरिदास हरि कृपा से,</span>
                        <span class="dprow">अवसि भक्ति को पाय॥</span>
                    </p>
                    <p></p>
                </div>

                
                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम चालीसा जो पढ़े,</span>
                        <span class="dprow">राम चरण चित लाय।</span>
                    </p>
                    <p>
                        <span class="dprow">जो इच्छा मन में करै,</span>
                        <span class="dprow">सकल सिद्ध हो जाय॥</span>
                    </p>
                    <p></p>
                </div>

