                <h2 class="dptitle" style="text-align: left;">॥ छप्पय ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सिंधु तरन, सिय-सोच हरन,</span>
                        <span class="dprow">रबि बाल बरन तनु।</span>
                    </p>
                    <p>
                        <span class="dprow">भुज बिसाल,</span>
                        <span class="dprow">मूरति कराल कालहु को काल जनु॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">गहन-दहन-निरदहन लंक निःसंक,</span>
                        <span class="dprow">बंक-भुव।</span>
                    </p>
                    <p>
                        <span class="dprow">जातुधान-बलवान,</span>
                        <span class="dprow">मान-मद-दवन पवनसुव॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कह तुलसिदास सेवत सुलभ,</span>
                        <span class="dprow">सेवक हित सन्तत निकट।</span>
                    </p>
                    <p>
                        <span class="dprow">गुन गनत, नमत, सुमिरत जपत,</span>
                        <span class="dprow">समन सकल-संकट-विकट॥1॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">स्वर्न-सैल-संकास कोटि-रवि,</span>
                        <span class="dprow">तरुन तेज घन।</span>
                    </p>
                    <p>
                        <span class="dprow">उर विसाल भुज दण्ड,</span>
                        <span class="dprow">चण्ड नख-वज्रतन॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पिंग नयन, भृकुटी कराल,</span>
                        <span class="dprow">रसना दसनानन।</span>
                    </p>
                    <p>
                        <span class="dprow">कपिस केस करकस लंगूर,</span>
                        <span class="dprow">खल-दल-बल-भानन॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कह तुलसिदास बस जासु उर,</span>
                        <span class="dprow">मारुतसुत मूरति विकट।</span>
                    </p>
                    <p>
                        <span class="dprow">संताप पाप तेहि पुरुष पहि,</span>
                        <span class="dprow">सपनेहुँ नहिं आवत निकट॥2॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ झूलना ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पञ्चमुख-छःमुख भृगु मुख्य भट असुर सुर,</span>
                        <span class="dprow">सर्व सरि समर समरत्थ सूरो।</span>
                    </p>
                    <p>
                        <span class="dprow">बांकुरो बीर बिरुदैत बिरुदावली,</span>
                        <span class="dprow">बेद बंदी बदत पैजपूरो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जासु गुनगाथ रघुनाथ कह जासुबल,</span>
                        <span class="dprow">बिपुल जल भरित जग जलधि झूरो।</span>
                    </p>
                    <p>
                        <span class="dprow">दुवन दल दमन को कौन तुलसीस है,</span>
                        <span class="dprow">पवन को पूत रजपूत रुरो॥3॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ घनाक्षरी  ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भानुसों पढ़न हनुमान गए भानुमन,</span>
                        <span class="dprow">अनुमानि सिसु केलि कियो फेर फारसो।</span>
                    </p>
                    <p>
                        <span class="dprow">पाछिले पगनि गम गगन मगन मन,</span>
                        <span class="dprow">क्रम को न भ्रम कपि बालक बिहार सो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कौतुक बिलोकि लोकपाल हरिहर विधि,</span>
                        <span class="dprow">लोचननि चकाचौंधी चित्तनि खबार सो।</span>
                    </p>
                    <p>
                        <span class="dprow">बल कैंधो बीर रस धीरज कै, साहस कै,</span>
                        <span class="dprow">तुलसी सरीर धरे सबनि सार सो॥4॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भारत में पारथ के रथ केथू कपिराज,</span>
                        <span class="dprow">गाज्यो सुनि कुरुराज दल हल बल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">कह्यो द्रोन भीषम समीर सुत महाबीर,</span>
                        <span class="dprow">बीर-रस-बारि-निधि जाको बल जल भो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बानर सुभाय बाल केलि भूमि भानु लागि,</span>
                        <span class="dprow">फलँग फलाँग हूतें घाटि नभ तल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">नाई-नाई-माथ जोरि-जोरि हाथ जोधा जो हैं,</span>
                        <span class="dprow">हनुमान देखे जगजीवन को फल भो॥5॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">गो-पद पयोधि करि, होलिका ज्यों लाई लंक,</span>
                        <span class="dprow">निपट निःसंक पर पुर गल बल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">द्रोन सो पहार लियो ख्याल ही उखारि कर,</span>
                        <span class="dprow">कंदुक ज्यों कपि खेल बेल कैसो फल भो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">संकट समाज असमंजस भो राम राज,</span>
                        <span class="dprow">काज जुग पूगनि को करतल पल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">साहसी समत्थ तुलसी को नाई जा की बाँह,</span>
                        <span class="dprow">लोक पाल पालन को फिर थिर थल भो॥6॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कमठ की पीठि जाके गोडनि की गाड़ऐं मानो,</span>
                        <span class="dprow">नाप के भाजन भरि जल निधि जल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">जातुधान दावन परावन को दुर्ग भयो,</span>
                        <span class="dprow">महा मीन बास तिमि तोमनि को थल भो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कुम्भकरन रावन पयोद नाद ईधन को,</span>
                        <span class="dprow">तुलसी प्रताप जाको प्रबल अनल भो।</span>
                    </p>
                    <p>
                        <span class="dprow">भीषम कहत मेरे अनुमान हनुमान,</span>
                        <span class="dprow">सारिखो त्रिकाल न त्रिलोक महाबल भो॥7॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">दूत राम राय को सपूत पूत पौनको तू,</span>
                        <span class="dprow">अंजनी को नन्दन प्रताप भूरि भानु सो।</span>
                    </p>
                    <p>
                        <span class="dprow">सीय-सोच-समन, दुरित दोष दमन,</span>
                        <span class="dprow">सरन आये अवन लखन प्रिय प्राण सो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">दसमुख दुसह दरिद्र दरिबे को भयो,</span>
                        <span class="dprow">प्रकट तिलोक ओक तुलसी निधान सो।</span>
                    </p>
                    <p>
                        <span class="dprow">ज्ञान गुनवान बलवान सेवा सावधान,</span>
                        <span class="dprow">साहेब सुजान उर आनु हनुमान सो॥8॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">दवन दुवन दल भुवन बिदित बल,</span>
                        <span class="dprow">बेद जस गावत बिबुध बंदी छोर को।</span>
                    </p>
                    <p>
                        <span class="dprow">पाप ताप तिमिर तुहिन निघटन पटु,</span>
                        <span class="dprow">सेवक सरोरुह सुखद भानु भोर को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">लोक परलोक तें बिसोक सपने न सोक,</span>
                        <span class="dprow">तुलसी के हिये है भरोसो एक ओर को।</span>
                    </p>
                    <p>
                        <span class="dprow">राम को दुलारो दास बामदेव को निवास,</span>
                        <span class="dprow">नाम कलि कामतरु केसरी किसोर को॥9॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">महाबल सीम महा भीम महाबान इत,</span>
                        <span class="dprow">महाबीर बिदित बरायो रघुबीर को।</span>
                    </p>
                    <p>
                        <span class="dprow">कुलिस कठोर तनु जोर परै रोर रन,</span>
                        <span class="dprow">करुना कलित मन धारमिक धीर को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">दुर्जन को कालसो कराल पाल सज्जन को,</span>
                        <span class="dprow">सुमिरे हरन हार तुलसी की पीर को।</span>
                    </p>
                    <p>
                        <span class="dprow">सीय-सुख-दायक दुलारो रघुनायक को,</span>
                        <span class="dprow">सेवक सहायक है साहसी समीर को॥10॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">रचिबे को बिधि जैसे, पालिबे को हरि हर,</span>
                        <span class="dprow">मीच मारिबे को, ज्याईबे को सुधापान भो।</span>
                    </p>
                    <p>
                        <span class="dprow">धरिबे को धरनि, तरनि तम दलिबे को,</span>
                        <span class="dprow">सोखिबे कृसानु पोषिबे को हिम भानु भो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">खल दुःख दोषिबे को, जन परितोषिबे को,</span>
                        <span class="dprow">माँगिबो मलीनता को मोदक दुदान भो।</span>
                    </p>
                    <p>
                        <span class="dprow">आरत की आरति निवारिबे को तिहुँ पुर,</span>
                        <span class="dprow">तुलसी को साहेब हठीलो हनुमान भो॥11॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सेवक स्योकाई जानि जानकीस मानै कानि,</span>
                        <span class="dprow">सानुकूल सूलपानि नवै नाथ नाँक को।</span>
                    </p>
                    <p>
                        <span class="dprow">देवी देव दानव दयावने ह्वै जोरैं हाथ,</span>
                        <span class="dprow">बापुरे बराक कहा और राजा राँक को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जागत सोवत बैठे बागत बिनोद मोद,</span>
                        <span class="dprow">ताके जो अनर्थ सो समर्थ एक आँक को।</span>
                    </p>
                    <p>
                        <span class="dprow">सब दिन रुरो परै पूरो जहाँ तहाँ ताहि,</span>
                        <span class="dprow">जाके है भरोसो हिये हनुमान हाँक को॥12॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सानुग सगौरि सानुकूल सूलपानि ताहि,</span>
                        <span class="dprow">लोकपाल सकल लखन राम जानकी।</span>
                    </p>
                    <p>
                        <span class="dprow">लोक परलोक को बिसोक सो तिलोक ताहि,</span>
                        <span class="dprow">तुलसी तमाइ कहा काहू बीर आनकी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">केसरी किसोर बन्दीछोर के नेवाजे सब,</span>
                        <span class="dprow">कीरति बिमल कपि करुनानिधान की।</span>
                    </p>
                    <p>
                        <span class="dprow">बालक ज्यों पालि हैं कृपालु मुनि सिद्धता को,</span>
                        <span class="dprow">जाके हिये हुलसति हाँक हनुमान की॥13॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">करुनानिधान बलबुद्धि के निधान हौ,</span>
                        <span class="dprow">महिमा निधान गुनज्ञान के निधान हौ।</span>
                    </p>
                    <p>
                        <span class="dprow">बाम देव रुप भूप राम के सनेही, नाम,</span>
                        <span class="dprow">लेत देत अर्थ धर्म काम निरबान हौ॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">आपने प्रभाव सीताराम के सुभाव सील,</span>
                        <span class="dprow">लोक बेद बिधि के बिदूष हनुमान हौ।</span>
                    </p>
                    <p>
                        <span class="dprow">मन की बचन की करम की तिहूँ प्रकार,</span>
                        <span class="dprow">तुलसी तिहारो तुम साहेब सुजान हौ॥14॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">मन को अगम तन सुगम किये कपीस,</span>
                        <span class="dprow">काज महाराज के समाज साज साजे हैं।</span>
                    </p>
                    <p>
                        <span class="dprow">देवबंदी छोर रनरोर केसरी किसोर,</span>
                        <span class="dprow">जुग जुग जग तेरे बिरद बिराजे हैं॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बीर बरजोर घटि जोर तुलसी की ओर,</span>
                        <span class="dprow">सुनि सकुचाने साधु खल गन गाजे हैं।</span>
                    </p>
                    <p>
                        <span class="dprow">बिगरी सँवार अंजनी कुमार कीजे मोहिं,</span>
                        <span class="dprow">जैसे होत आये हनुमान के निवाजे हैं॥15॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ सवैया ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जान सिरोमनि हो हनुमान,</span>
                        <span class="dprow">सदा जन के मन बास तिहारो।</span>
                    </p>
                    <p>
                        <span class="dprow">ढ़आरो बिगारो मैं काको कहा,</span>
                        <span class="dprow">केहि कारन खीझत हौं तो तिहारो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">साहेब सेवक नाते तो हातो,</span>
                        <span class="dprow">कियो सो तहां तुलसी को न चारो।</span>
                    </p>
                    <p>
                        <span class="dprow">दोष सुनाये तैं आगेहुँ को,</span>
                        <span class="dprow">होशियार ह्वैं हों मन तो हिय हारो॥16॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तेरे थपै उथपै न महेस,</span>
                        <span class="dprow">थपै थिर को कपि जे उर घाले।</span>
                    </p>
                    <p>
                        <span class="dprow">तेरे निबाजे गरीब निबाज,</span>
                        <span class="dprow">बिराजत बैरिन के उर साले॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">संकट सोच सबै तुलसी,</span>
                        <span class="dprow">लिये नाम फटै मकरी के से जाले।</span>
                    </p>
                    <p>
                        <span class="dprow">बूढ भये बलि मेरिहिं बार,</span>
                        <span class="dprow">कि हारि परे बहुतै नत पाले॥17॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सिंधु तरे बड़ए बीर दले खल,</span>
                        <span class="dprow">जारे हैं लंक से बंक मवासे।</span>
                    </p>
                    <p>
                        <span class="dprow">तैं रनि केहरि केहरि के,</span>
                        <span class="dprow">बिदले अरि कुंजर छैल छवासे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तोसो समत्थ सुसाहेब सेई सहै,</span>
                        <span class="dprow">तुलसी दुख दोष दवा से।</span>
                    </p>
                    <p>
                        <span class="dprow">बानरबाज ! बढ़ए खल खेचर,</span>
                        <span class="dprow">लीजत क्यों न लपेटि लवासे॥18॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अच्छ विमर्दन कानन भानि,</span>
                        <span class="dprow">दसानन आनन भा न निहारो।</span>
                    </p>
                    <p>
                        <span class="dprow">बारिदनाद अकंपन कुंभकरन से,</span>
                        <span class="dprow">कुञ्जर केहरि वारो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम प्रताप हुतासन, कच्छ,</span>
                        <span class="dprow">विपच्छ, समीर समीर दुलारो।</span>
                    </p>
                    <p>
                        <span class="dprow">पाप ते साप ते ताप तिहूँ तें,</span>
                        <span class="dprow">सदा तुलसी कह सो रखवारो॥19॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ घनाक्षरी ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जानत जहान हनुमान को निवाज्यो जन,</span>
                        <span class="dprow">मन अनुमानि बलि बोल न बिसारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">सेवा जोग तुलसी कबहुँ कहा चूक परी,</span>
                        <span class="dprow">साहेब सुभाव कपि साहिबी संभारिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अपराधी जानि कीजै सासति सहस भान्ति,</span>
                        <span class="dprow">मोदक मरै जो ताहि माहुर न मारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">साहसी समीर के दुलारे रघुबीर जू के,</span>
                        <span class="dprow">बाँह पीर महाबीर बेगि ही निवारिये॥20॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बालक बिलोकि, बलि बारें तें आपनो कियो,</span>
                        <span class="dprow">दीनबन्धु दया कीन्हीं निरुपाधि न्यारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">रावरो भरोसो तुलसी के, रावरोई बल,</span>
                        <span class="dprow">आस रावरीयै दास रावरो विचारिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बड़ओ बिकराल कलि काको न बिहाल कियो,</span>
                        <span class="dprow">माथे पगु बलि को निहारि सो निबारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">केसरी किसोर रनरोर बरजोर बीर,</span>
                        <span class="dprow">बाँह पीर राहु मातु ज्यौं पछारि मारिये॥21॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">उथपे थपनथिर थपे उथपनहार,</span>
                        <span class="dprow">केसरी कुमार बल आपनो संबारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">राम के गुलामनि को काम तरु रामदूत,</span>
                        <span class="dprow">मोसे दीन दूबरे को तकिया तिहारिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">साहेब समर्थ तो सों तुलसी के माथे पर,</span>
                        <span class="dprow">सोऊ अपराध बिनु बीर, बाँधि मारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">पोखरी बिसाल बाँहु, बलि, बारिचर पीर,</span>
                        <span class="dprow">मकरी ज्यों पकरि के बदन बिदारिये॥22॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम को सनेह, राम साहस लखन सिय,</span>
                        <span class="dprow">राम की भगति, सोच संकट निवारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">मुद मरकट रोग बारिनिधि हेरि हारे,</span>
                        <span class="dprow">जीव जामवंत को भरोसो तेरो भारिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कूदिये कृपाल तुलसी सुप्रेम पब्बयतें,</span>
                        <span class="dprow">सुथल सुबेल भालू बैठि कै विचारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">महाबीर बाँकुरे बराकी बाँह पीर क्यों न,</span>
                        <span class="dprow">लंकिनी ज्यों लात घात ही मरोरि मारिये॥23॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">लोक परलोकहुँ तिलोक न विलोकियत,</span>
                        <span class="dprow">तोसे समरथ चष चारिहूँ निहारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">कर्म, काल, लोकपाल, अग जग जीवजाल,</span>
                        <span class="dprow">नाथ हाथ सब निज महिमा बिचारिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">खास दास रावरो, निवास तेरो तासु उर,</span>
                        <span class="dprow">तुलसी सो, देव दुखी देखिअत भारिये।</span>
                    </p>
                    <p>
                        <span class="dprow">बात तरुमूल बाँहूसूल कपिकच्छु बेलि,</span>
                        <span class="dprow">उपजी सकेलि कपि केलि ही उखारिये॥24॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">करम कराल कंस भूमिपाल के भरोसे,</span>
                        <span class="dprow">बकी बक भगिनी काहू तें कहा डरैगी।</span>
                    </p>
                    <p>
                        <span class="dprow">बड़ई बिकराल बाल घातिनी न जात कहि,</span>
                        <span class="dprow">बाँहू बल बालक छबीले छोटे छरैगी॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">आई है बनाई बेष आप ही बिचारि देख,</span>
                        <span class="dprow">पाप जाय सब को गुनी के पाले परैगी।</span>
                    </p>
                    <p>
                        <span class="dprow">पूतना पिसाचिनी ज्यौं कपि कान्ह तुलसी की,</span>
                        <span class="dprow">बाँह पीर महाबीर तेरे मारे मरैगी॥25॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">भाल की कि काल की कि रोष की त्रिदोष की है,</span>
                        <span class="dprow">बेदन बिषम पाप ताप छल छाँह की।</span>
                    </p>
                    <p>
                        <span class="dprow">करमन कूट की कि जन्त्र मन्त्र बूट की,</span>
                        <span class="dprow">पराहि जाहि पापिनी मलीन मन माँह की॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पैहहि सजाय, नत कहत बजाय तोहि,</span>
                        <span class="dprow">बाबरी न होहि बानि जानि कपि नाँह की।</span>
                    </p>
                    <p>
                        <span class="dprow">आन हनुमान की दुहाई बलवान की,</span>
                        <span class="dprow">सपथ महाबीर की जो रहै पीर बाँह की॥26॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सिंहिका सँहारि बल सुरसा सुधारि छल,</span>
                        <span class="dprow">लंकिनी पछारि मारि बाटिका उजारी है।</span>
                    </p>
                    <p>
                        <span class="dprow">लंक परजारि मकरी बिदारि बार बार,</span>
                        <span class="dprow">जातुधान धारि धूरि धानी करि डारी है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तोरि जमकातरि मंदोदरी कठोरि आनी,</span>
                        <span class="dprow">रावन की रानी मेघनाद महतारी है।</span>
                    </p>
                    <p>
                        <span class="dprow">भीर बाँह पीर की निपट राखी महाबीर,</span>
                        <span class="dprow">कौन के सकोच तुलसी के सोच भारी है॥27॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तेरो बालि केलि बीर सुनि सहमत धीर,</span>
                        <span class="dprow">भूलत सरीर सुधि सक्र रवि राहु की।</span>
                    </p>
                    <p>
                        <span class="dprow">तेरी बाँह बसत बिसोक लोक पाल सब,</span>
                        <span class="dprow">तेरो नाम लेत रहैं आरति न काहु की॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">साम दाम भेद विधि बेदहू लबेद सिधि,</span>
                        <span class="dprow">हाथ कपिनाथ ही के चोटी चोर साहु की।</span>
                    </p>
                    <p>
                        <span class="dprow">आलस अनख परिहास कै सिखावन है,</span>
                        <span class="dprow">एते दिन रही पीर तुलसी के बाहु की॥28॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">टूकनि को घर घर डोलत कँगाल बोलि,</span>
                        <span class="dprow">बाल ज्यों कृपाल नत पाल पालि पोसो है।</span>
                    </p>
                    <p>
                        <span class="dprow">कीन्ही है सँभार सार अँजनी कुमार बीर,</span>
                        <span class="dprow">आपनो बिसारि हैं न मेरेहू भरोसो है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">इतनो परेखो सब भान्ति समरथ आजु,</span>
                        <span class="dprow">कपिराज सांची कहौं को तिलोक तोसो है।</span>
                    </p>
                    <p>
                        <span class="dprow">सासति सहत दास कीजे पेखि परिहास,</span>
                        <span class="dprow">चीरी को मरन खेल बालकनि कोसो है॥29॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">आपने ही पाप तें त्रिपात तें कि साप तें,</span>
                        <span class="dprow">बढ़ई है बाँह बेदन कही न सहि जाति है।</span>
                    </p>
                    <p>
                        <span class="dprow">औषध अनेक जन्त्र मन्त्र टोटकादि किये,</span>
                        <span class="dprow">बादि भये देवता मनाये अधीकाति है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">करतार, भरतार, हरतार, कर्म काल,</span>
                        <span class="dprow">को है जगजाल जो न मानत इताति है।</span>
                    </p>
                    <p>
                        <span class="dprow">चेरो तेरो तुलसी तू मेरो कह्यो राम दूत,</span>
                        <span class="dprow">ढील तेरी बीर मोहि पीर तें पिराति है॥30॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">दूत राम राय को, सपूत पूत वाय को,</span>
                        <span class="dprow">समत्व हाथ पाय को सहाय असहाय को।</span>
                    </p>
                    <p>
                        <span class="dprow">बाँकी बिरदावली बिदित बेद गाइयत,</span>
                        <span class="dprow">रावन सो भट भयो मुठिका के धाय को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">एते बडे साहेब समर्थ को निवाजो आज,</span>
                        <span class="dprow">सीदत सुसेवक बचन मन काय को।</span>
                    </p>
                    <p>
                        <span class="dprow">थोरी बाँह पीर की बड़ई गलानि तुलसी को,</span>
                        <span class="dprow">कौन पाप कोप, लोप प्रकट प्रभाय को॥31॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">देवी देव दनुज मनुज मुनि सिद्ध नाग,</span>
                        <span class="dprow">छोटे बड़ए जीव जेते चेतन अचेत हैं।</span>
                    </p>
                    <p>
                        <span class="dprow">पूतना पिसाची जातुधानी जातुधान बाग,</span>
                        <span class="dprow">राम दूत की रजाई माथे मानि लेत हैं॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">घोर जन्त्र मन्त्र कूट कपट कुरोग जोग,</span>
                        <span class="dprow">हनुमान आन सुनि छाड़त निकेत हैं।</span>
                    </p>
                    <p>
                        <span class="dprow">क्रोध कीजे कर्म को प्रबोध कीजे तुलसी को,</span>
                        <span class="dprow">सोध कीजे तिनको जो दोष दुख देत हैं॥32॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तेरे बल बानर जिताये रन रावन सों,</span>
                        <span class="dprow">तेरे घाले जातुधान भये घर घर के।</span>
                    </p>
                    <p>
                        <span class="dprow">तेरे बल राम राज किये सब सुर काज,</span>
                        <span class="dprow">सकल समाज साज साजे रघुबर के॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">तेरो गुनगान सुनि गीरबान पुलकत,</span>
                        <span class="dprow">सजल बिलोचन बिरंचि हरिहर के।</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसी के माथे पर हाथ फेरो कीस नाथ,</span>
                        <span class="dprow">देखिये न दास दुखी तोसो कनिगर के॥33॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पालो तेरे टूक को परेहू चूक मूकिये न,</span>
                        <span class="dprow">कूर कौड़ई दूको हौं आपनी ओर हेरिये।</span>
                    </p>
                    <p>
                        <span class="dprow">भोरानाथ भोरे ही सरोष होत थोरे दोष,</span>
                        <span class="dprow">पोषि तोषि थापि आपनो न अव डेरिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">अँबु तू हौं अँबु चूर, अँबु तू हौं डिंभ सो न,</span>
                        <span class="dprow">बूझिये बिलंब अवलंब मेरे तेरिये।</span>
                    </p>
                    <p>
                        <span class="dprow">बालक बिकल जानि पाहि प्रेम पहिचानि,</span>
                        <span class="dprow">तुलसी की बाँह पर लामी लूम फेरिये॥34॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">घेरि लियो रोगनि, कुजोगनि, कुलोगनि ज्यौं,</span>
                        <span class="dprow">बासर जलद घन घटा धुकि धाई है।</span>
                    </p>
                    <p>
                        <span class="dprow">बरसत बारि पीर जारिये जवासे जस,</span>
                        <span class="dprow">रोष बिनु दोष धूम मूल मलिनाई है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">करुनानिधान हनुमान महा बलवान,</span>
                        <span class="dprow">हेरि हँसि हाँकि फूंकि फौंजै ते उड़आई है।</span>
                    </p>
                    <p>
                        <span class="dprow">खाये हुतो तुलसी कुरोग राढ़ राकसनि,</span>
                        <span class="dprow">केसरी किसोर राखे बीर बरिआई है॥35॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ सवैया ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">राम गुलाम तु ही हनुमान,</span>
                        <span class="dprow">गोसाँई सुसाँई सदा अनुकूलो।</span>
                    </p>
                    <p>
                        <span class="dprow">पाल्यो हौं बाल ज्यों आखर दू,</span>
                        <span class="dprow">पितु मातु सों मंगल मोद समूलो॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बाँह की बेदन बाँह पगार,</span>
                        <span class="dprow">पुकारत आरत आनँद भूलो।</span>
                    </p>
                    <p>
                        <span class="dprow">श्री रघुबीर निवारिये पीर,</span>
                        <span class="dprow">रहौं दरबार परो लटि लूलो॥36॥</span>
                    </p>
                    <p></p>
                </div>

                <h2 class="dptitle" style="text-align: left;">॥ घनाक्षरी ॥</h2>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">काल की करालता करम कठिनाई कीधौ,</span>
                        <span class="dprow">पाप के प्रभाव की सुभाय बाय बावरे।</span>
                    </p>
                    <p>
                        <span class="dprow">बेदन कुभाँति सो सही न जाति राति दिन,</span>
                        <span class="dprow">सोई बाँह गही जो गही समीर डाबरे॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">लायो तरु तुलसी तिहारो सो निहारि बारि,</span>
                        <span class="dprow">सींचिये मलीन भो तयो है तिहुँ तावरे।</span>
                    </p>
                    <p>
                        <span class="dprow">भूतनि की आपनी पराये की कृपा निधान,</span>
                        <span class="dprow">जानियत सबही की रीति राम रावरे॥37॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">पाँय पीर पेट पीर बाँह पीर मुंह पीर,</span>
                        <span class="dprow">जर जर सकल पीर मई है।</span>
                    </p>
                    <p>
                        <span class="dprow">देव भूत पितर करम खल काल ग्रह,</span>
                        <span class="dprow">मोहि पर दवरि दमानक सी दई है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">हौं तो बिनु मोल के बिकानो बलि बारे हीतें,</span>
                        <span class="dprow">ओट राम नाम की ललाट लिखि लई है।</span>
                    </p>
                    <p>
                        <span class="dprow">कुँभज के किंकर बिकल बूढ़ए गोखुरनि,</span>
                        <span class="dprow">हाय राम राय ऐसी हाल कहूँ भई है॥38॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बाहुक सुबाहु नीच लीचर मरीच मिलि,</span>
                        <span class="dprow">मुँह पीर केतुजा कुरोग जातुधान है।</span>
                    </p>
                    <p>
                        <span class="dprow">राम नाम जप जाग कियो चहों सानुराग,</span>
                        <span class="dprow">काल कैसे दूत भूत कहा मेरे मान है॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सुमिरे सहाय राम लखन आखर दौऊ,</span>
                        <span class="dprow">जिनके समूह साके जागत जहान है।</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसी सँभारि ताडका सँहारि भारि भट,</span>
                        <span class="dprow">बेधे बरगद से बनाई बानवान है॥39॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">बालपने सूधे मन राम सनमुख भयो,</span>
                        <span class="dprow">राम नाम लेत माँगि खात टूक टाक हौं।</span>
                    </p>
                    <p>
                        <span class="dprow">परयो लोक रीति में पुनीत प्रीति राम राय,</span>
                        <span class="dprow">मोह बस बैठो तोरि तरकि तराक हौं॥</span>
                    </p>
                    <p></p>
                </div>
                
                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">खोटे खोटे आचरन आचरत अपनायो,</span>
                        <span class="dprow">अंजनी कुमार सोध्यो रामपानि पाक हौं।</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसी गुसाँई भयो भोंडे दिन भूल गयो,</span>
                        <span class="dprow">ताको फल पावत निदान परिपाक हौं॥40॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">असन बसन हीन बिषम बिषाद लीन,</span>
                        <span class="dprow">देखि दीन दूबरो करै न हाय हाय को।</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसी अनाथ सो सनाथ रघुनाथ कियो,</span>
                        <span class="dprow">दियो फल सील सिंधु आपने सुभाय को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">नीच यहि बीच पति पाइ भरु हाईगो,</span>
                        <span class="dprow">बिहाइ प्रभु भजन बचन मन काय को।</span>
                    </p>
                    <p>
                        <span class="dprow">ता तें तनु पेषियत घोर बरतोर मिस,</span>
                        <span class="dprow">फूटि फूटि निकसत लोन राम राय को॥41॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">जीओ जग जानकीजीवन को कहाइ जन,</span>
                        <span class="dprow">मरिबे को बारानसी बारि सुरसरि को।</span>
                    </p>
                    <p>
                        <span class="dprow">तुलसी के दोहूँ हाथ मोदक हैं ऐसे ठाँऊ,</span>
                        <span class="dprow">जाके जिये मुये सोच करिहैं न लरि को॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">मोको झूँटो साँचो लोग राम कौ कहत सब,</span>
                        <span class="dprow">मेरे मन मान है न हर को न हरि को।</span>
                    </p>
                    <p>
                        <span class="dprow">भारी पीर दुसह सरीर तें बिहाल होत,</span>
                        <span class="dprow">सोऊ रघुबीर बिनु सकै दूर करि को॥42॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">सीतापति साहेब सहाय हनुमान नित,</span>
                        <span class="dprow">हित उपदेश को महेस मानो गुरु कै।</span>
                    </p>
                    <p>
                        <span class="dprow">मानस बचन काय सरन तिहारे पाँय,</span>
                        <span class="dprow">तुम्हरे भरोसे सुर मैं न जाने सुर कै॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">ब्याधि भूत जनित उपाधि काहु खल की,</span>
                        <span class="dprow">समाधि की जै तुलसी को जानि जन फुर कै।</span>
                    </p>
                    <p>
                        <span class="dprow">कपिनाथ रघुनाथ भोलानाथ भूतनाथ,</span>
                        <span class="dprow">रोग सिंधु क्यों न डारियत गाय खुर कै॥43॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">कहों हनुमान सों सुजान राम राय सों,</span>
                        <span class="dprow">कृपानिधान संकर सों सावधान सुनिये।</span>
                    </p>
                    <p>
                        <span class="dprow">हरष विषाद राग रोष गुन दोष मई,</span>
                        <span class="dprow">बिरची बिरञ्ची सब देखियत दुनिये॥</span>
                    </p>
                    <p></p>
                </div>

                <div class="dpaartigroup">
                    <p>
                        <span class="dprow">माया जीव काल के करम के सुभाय के,</span>
                        <span class="dprow">करैया राम बेद कहें साँची मन गुनिये।</span>
                    </p>
                    <p>
                        <span class="dprow">तुम्ह तें कहा न होय हा हा सो बुझैये मोहिं,</span>
                        <span class="dprow">हौं हूँ रहों मौनही वयो सो जानि लुनिये॥44॥</span>
                    </p>
                    <p></p>
                </div>

