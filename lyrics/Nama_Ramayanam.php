    <p class="para">The Ramayana is a great Sanskrit epic poem written by Sage Valmiki. The Ramayana consists of 24,000 verses which are divided in seven books and 500 cantos. The conjunction of Rama and Ayana makes the word Ramayana which means The Journey of Lord Rama. Lord Rama is considered the 7th incarnation of Lord Vishnu.</p>
    <p class="para">For Hindus, the Ramayana is a great religious book. It is widely believed that reciting Ramayana brings peace and prosperity into the family. Reciting whole Ramayana might take couple of days and many times due to time constraint it is not possible to recite the whole Ramayana in a single sitting.</p>
    <p class="para">Nama Ramayanam is the condensed version of epic Ramayana written by sage Valmiki in Sanskrit. Nama Ramayanam consists of 108 Shlokas and similar to complete Ramayana, Nama Ramayanam has seven chapters which are divided into Balakandah, Ayodhyakandah, Kishkindhakandah, Sundarkandah, Yuddhakandah and Uttarakandah.</p>
    <p class="para">Nama Ramayanam is very popular in South Indian states. Tamil Nama Ramayanam, Telugu Nama Ramayanam, Kannada Nama Ramayana and Malayalam Nama Ramayanam are very popular in Tamil Nadu, Andhra Pradesh & Telangana, Karnataka and Kerala respectively. Due to popularity of Ramacharitamanas in Hindi speaking regions, Nama Ramayanam is less popular in North Indian states. It should be noted that Nama Ramayana is not the condensed version of Ramacharitamanas written by Sant Tulsidas in Awadhi dialect of Hindi.</p>
    <p class="para">There is even Eka Shloki Ramayanam, in which the whole epic Ramayana is narrated in a single Shloka.</p>

