            <li class="dplist">Deepdurga</li>
        </ol>
        <p class="dptitle">Navratri Date and Time</p>
        <p class="dpcontent">Navratri begins on the first day of Ashwin lunar month with Ghatasthapana. Kalash, which is installed during Ghatasthapana, is immersed in water body on the tenth day.</p>
        <p class="dpcontent">Navratri begins - Pratipada (1<sup>th</sup> day) of Ashwin (seventh lunar month) Shukla Paksha</p>
        <p class="dpcontent">Navratri ends - Navami (9<sup>th</sup> day) of Ashwin Shukla Paksha</p>
        <p class="dptitle">Navratri Festivals List</p>
        <ul class="dpcontent">
            <li class="dplist">Day 1 - Ghatasthapana, Shailaputri Puja</li>
            <li class="dplist">Day 2 - Brahmacharini Puja</li>
            <li class="dplist">Day 3 - Chandraghanta Puja</li>
            <li class="dplist">Day 4 - Kushmanda Puja</li>
            <li class="dplist">Day 5 - Upang Lalita Vrat, Skandamata Puja</li>
            <li class="dplist">Day 6 - Katyayani Puja</li>
