            <li class="dplist">Day 8 - Mahashtami, Durgashtami, Kumari Puja, Mahagauri Puja</li>
            <li class="dplist">Day 9 - Maha Navami, Navami Homa, Siddhidatri Puja</li>
        </ul>
        <p class="dptitle">Navratri Observance</p>
        <p class="dpcontent">Numerous rituals are followed during Navratri. These rituals vary from state to state and within a state region to region. However, we are listing the most common rituals which are observed and traditions which are followed during Navratri.</p>
        <ul class="dpcontent">
            <li class="dplist">Ghatasthapana on the first day of Navratri</li>
            <li class="dplist">worshipping 9 forms of Goddess Durga</li>
            <li class="dplist">9 days long Navratri fast</li>
            <li class="dplist">sowing Sapta-Dhanya i.e. seven grains</li>
            <li class="dplist">installing Jyoti Kalash for 9 days</li>
