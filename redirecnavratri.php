        <p class="dpcontent">As per Mahakala Samhita, there are four Navratri in Hindu calendar.</p>
        <ul class="dpcontent">
            <li class="dplist">Sharad Navratri</li>
            <li class="dplist">Chaitra Navratri</li>
            <li class="dplist">Magha Gupta Navratri</li>
            <li class="dplist">Ashadha Gupta Navratri</li>
        </ul>
        <p class="dpcontent">As per the religious text, the significance of each Navratri varies from Yuga to Yuga. In Satya Yuga Vasant Navratri during Chaitra lunar month becomes the most significant among all four Navratri. Similarly, in Treta Yuga Gupta Navratri during Ashadha lunar month, in Dvapara Yuga Gupta Navratri during Magha lunar month and in Kali Yuga Sharad Navratri during Ashwin lunar month becomes the most significant among all four Navratri.</p>
        <p class="dptitle">Navratri Origin | Significance</p>
        <p class="dpcontent">During Sharadiya Navratri, Goddess Durga killed mighty demon Mahishasura. Different religious texts mention separate form of Goddess Durga who killed Mahishasura. As per Rambha Kalpa, Goddess Durga in the form of 18 handed Ugrachandi killed Mahishasura. However, as per Neelalohita Kalpa, 16 handed Bhadrakali killed Mahishasura and as per Shwetavaraha Kalpa 10 handed Katyayani killed Mahishasura.</p>
        <p class="dpcontent">However, most religious texts agree that it was demon Mahishasura who was killed by one of the ferocious forms of Goddess Durga. Hence, the victory of Goddess Durga over evil is celebrated during Navratri.</p>
        <p class="dpcontent">It is also believed that Lord Rama also worshipped Goddess Durga during Sharad Navratri. On the advice of Lord Brahma, Lord Rama invoked Goddess Durga and sought her blessings before waging war against demon Ravana. As Lord Rama invoked Goddess Durga during her sleeping period, since then the untimely invocation of the Goddess Durga during Sharad Navratri is also known as Akal Bodhan of Goddess Durga.</p>
        <p class="dptitle">Navratri Deity(s)</p>
        <p class="dpcontent">The main Goddess of Navratri is Goddess Durga who is also known as Goddess Bhavani and Goddess Amba. All these forms of Durga are attributed to Goddess Parvati.</p>
        <p class="dpcontent">Apart from Goddess Durga, nine forms of Goddess Durga are worshipped during Navratri. Depending on the region and Sampradaya, the list of nine Goddesses which are worshipped during Navratri might differ.</p>
        <p class="dpcontent">As per Shaiva Sampradaya and the list of Navratri Goddesses which is mostly followed in North India, presiding deity of each Tithi from Pratipada to Navami during Navratri are as following -</p>
        <ol class="dpcontent">
            <li class="dplist">Shailaputri</li>
            <li class="dplist">Brahmacharini</li>
            <li class="dplist">Chandraghanta</li>
            <li class="dplist">Kushmanda</li>
            <li class="dplist">Skandamata</li>
            <li class="dplist">Katyayani</li>
            <li class="dplist">Kalaratri</li>
            <li class="dplist">Mahagauri</li>
            <li class="dplist">Siddhidatri</li>
        </ol>
        <p class="dpcontent">In South India, following nine forms of Goddess Durga are worshipped -</p>
        <ol class="dpcontent">
            <li class="dplist">Vanadurga</li>
            <li class="dplist">Shoolini</li>
            <li class="dplist">Jataveda</li>
