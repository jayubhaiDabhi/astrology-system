        <h2 class="dptitle" style="text-align:center;margin-bottom:15px;margin-top:15px">Rama Navami - the birth anniversary of Lord Rama</h2>
        <div class="separatorline"></div>
        <p class="dptitle">Rama Navami</p>
        <p class="dpcontent">Rama Navami is observed as the birth anniversary of Lord Rama. It is very significant day and devotees of Lord Rama keep a day long fast and worship Him with all rituals. Lord Rama is considered the seventh incarnation of Lord Vishnu.</p>
