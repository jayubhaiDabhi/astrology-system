        <p class="dpcontent">At some places, mainly in North India, the day coincides with the last day of Chaitra Navratri. Many people conduct Homa on Rama Navami and conclude nine days festivities of Chaitra Navratri. Hence Navratri Puja during Chaitra month is also known as Rama Navratri.</p>
        <p class="dptitle">Rama Navami Origin | Significance</p>
        <p class="dpcontent">Rama Navami is observed since the Era of Lord Rama. Hindus are celebrating Rama Navami since ages and no historic records are kept to count the exact years of the birth anniversary of Lord Rama. However, as per Vedic time keeping, Lord Rama was born about a million years ago during Treta Yuga.</p>
        <p class="dptitle">Rama Navami Deity(s)y</p>
        <p class="dpcontent">Lord Rama is the main deity who is worshipped during Rama Navami.</p>
        <p class="dpcontent">Along with Lord Rama, the mother of Lord Rama i.e. Mata Kaushalya, the father of Lord Rama i.e. King Dasharatha, the wife of Lord Rama i.e. Devi Sita, three younger brothers of Lord Rama i.e. Bharata, Lakshmana and Shatrughna and the ardent devotee of Lord Rama i.e. Bhakta Hanuman are also worshipped on the auspicious day of Rama Navami.</p>
        <p class="dptitle">Rama Navami Date and Time</p>
        <p class="dpcontent">As per Amanta as well as Purnimanta Hindu Calendar -</p>
        <span class="dpred dpcontent">
            Shukla Paksha Navami (9<sup>th</sup> day) of Chaitra (1<sup>th</sup> month)
        </span>
        <p class="dptitle">Rama Navami Observance</p>
        <ul class="dpcontent">
            <li class="dplist">a day long fast</li>
            <li class="dplist">worshipping Lord Rama</li>
            <li class="dplist">listen or narrating the epic Ramayana or Nama Ramayanam</li>
            <li class="dplist">performing ceremonial wedding of Lord Rama and Goddess Sita</li>
            <li class="dplist">conducting Rama Navami procession</li>
            <li class="dplist">conducting Hawan i.e. Homa before breaking the fast on the next day</li>
        </ul>
        <p class="dptitle">Rama Navami Regional Variance</p>
        <p class="dpcontent">Rama Navami is a very significant day and it is observed throughout India.</p>
        <ul class="dpcontent">
            <li class="dplist dptitle">Rama Navami in Uttar Pradesh</li>
            <p class="dpcontent">Ayodhya is the most significant place to celebrate the birth anniversary of Lord Rama. It is considered highly auspicious to take bath in sacred river Sarayu on Rama Navami. Thousands of devotees take a holy dip in the river of Sarayu before they visit famous Kanak Bhawan temple in Ayodhya. Being the birthplace of Lord Rama, Ayodhya during Rama Navami is similar to what Mathura is during Krishna Janmashtami.</p>
            <li class="dplist dptitle">Rama Navami in ISKCON</li>
            <p class="dpcontent">The day of Rama Navami is also very significant for ISKCON temples and its followers. Most ISKCON temples celebrate Rama Navami by organizing special events. Procession of Lord Rama and Goddess Sita, Abhishekam of Lord Rama and performing Homa are some of the rituals which are performed in most ISKCON temples.</p>
            <p class="dpcontent">Lord Rama, being the incarnation of Lord Vishnu, is as important as Lord Krishna for the followers of ISKCON.</p>
        </ul>
        <p class="dptitle">Rama Navami Public Life</p>
        <p class="dpcontent">Rama Navami is an optional gazetted holiday in India. However, in North India government offices, schools and colleges might keep one day off during Rama Navami.</p>
