      <p class="dpcontent">When Kumbha Mela is held at Ujjain, at the same time an Ardha Kumbha Yoga is considered to happen at Haridwar, but this Kumbha is given less importance compared to the other Kumbha. This is apparently due to the fact of holding a Kumbha Mela at Ujjain at the same time and that at Nasik only at difference of few months.</p>
      <p class="dpcontent">Hence to summarize the above table</p>
      <ul>
        <li>
          <p class="dpcontent">Kumbha Mela - held at all four places every 3 years</p>
        </li>
        <li>
          <p class="dpcontent">Ardha Kumbha Mela - held at Haridwar and Prayagraj every 6 years</p>
        </li>
        <li>
          <p class="dpcontent">Purna Kumbha Mela - held only at Prayagraj every 12 years</p>
        </li>
        <li>
          <p class="dpcontent">Maha Kumbha Mela - held only at Prayagraj every 144 years</p>
        </li>
      </ul>
      <p class="dpcontent">Generally, all the auspicious bathing days in Kumbh Mela are determined on the basis of lunar days like Purnima and Amavasya etc. However, in Haridwar Kumbha, the date of bathing on Mesha Sankranti and Vaisakhi is determined as per solar calendar.</p>
