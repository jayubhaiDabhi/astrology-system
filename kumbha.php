      <h2 class="dptitle">About Kumbha Mela</h2>
      <p class="dpcontent">The Kumbha Mela, which can also be referred as religious congregation of people including both men and women, is the largest religious gathering in the world. During the festival, Lakhs of people gather to have holy baths, especially on certain auspicious days, in the holy water of the river by the side of which this Mela is held. Apart from having holy bath, people gather to have Darshan of the Sadhus who come down to the Mela from Himalayas as well as from other parts of India.</p>
      <p class="dpcontent">The Kumbha Mela is held at four different locations by turn and continues for about a month. The time and place for holding this unique Mela is decided, unlike other solar festivals, not only on the position of the Sun in a Rashi division but also that of the planet Jupiter, also known as Brihaspati. The rules of holding Kumbha Mela are elaborated in following table.</p>
