    <div class="main1">
        <p class="content">Before Saturn becomes retrograde it enters into retrograde zone and starts slowing down. Before it becomes stationary and starts moving into backward direction, it keeps moving into forward direction with retarded speed. Saturn remains into stationary motion for some moments before it starts moving backward. The time when Saturn just starts moving backward is noted down during our calculations.</p>
        <p class="content">The same is true while Saturn becomes progressive i.e. when it starts moving into forward direction. Before Saturn becomes progressive it remains into retrograde zone and starts moving into forward direction till it gains full speed. The time when Saturn just starts moving into forward direction (gaining full speed and leaving retrograde zone takes some more time) is noted down during our calculations.</p>
        <p class="content">In sum, each retrograde zone has four points namely, Shadow, Retro Station, Direct Station and Release. We list only Retro Station and Direct Station for Saturn.</p>
