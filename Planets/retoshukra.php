        <p class="content">Before Venus becomes retrograde it enters into retrograde zone and starts slowing down. Before it becomes stationary and starts moving into backward direction, it keeps moving into forward direction with retarded speed. Venus remains into stationary motion for some moments before it starts moving backward. The time when Venus just starts moving backward is noted down during our calculations.</p>
        <p class="content">The same is true while Venus becomes progressive i.e. when it starts moving into forward direction. Before Venus becomes progressive it remains into retrograde zone and starts moving into forward direction till it gains full speed. The time when Venus just starts moving into forward direction (gaining full speed and leaving retrograde zone takes some more time) is noted down during our calculations.</p>
        <p class="content">In sum, each retrograde zone has four points namely, Shadow, Retro Station, Direct Station and Release. We list only Retro Station and Direct Station for Venus.</p>
