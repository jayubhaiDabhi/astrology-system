        <h2 class="dptitle">Diwali Origin and Significance</h2>
        <p class="dpcontent">Diwali is historically a Hindu religion festival having its origin in the Era of Lord Rama or probably even before that at the time of churning of milky ocean when Goddess Lakshmi came out as the boon to the Gods and whole humanity.</p>
        <p class="dpcontent">Hinduism, being the oldest religion whose history goes back to thousands of years, it is not surprising that numerous legends are associated with Diwali. However all of them signify the victory of light over darkness, knowledge over ignorance, good over evil and hope over despair.</p>
        <h2 class="dptitle">Diwali Deity(s)</h2>
        <p class="dpcontent">Various deities are worshipped and appeased during five days Diwali festivity. However Goddess Lakshmi, Lord Ganesha, Lord Kuber are the most prominent names which come to the mind when Diwali Puja is mentioned.</p>
        <p class="dpcontent">Lord Yamraj, God Dhanvantari, God Hanuman, Goddess Kali, Goddess Saraswati, Lord Krishna and Demon King Bali are other prominent deities who are worshipped during Diwali.</p>
        <h2 class="dptitle">Diwali Date and Time</h2>
        <p class="dpcontent">As per Amanta Hindu Calendar, five days Diwali festivity spans over two months.</p>
        <span class="dpred dpcontent">
        Diwali begins - Krishna Paksha Trayodashi (28<sup>th</sup>day) of Ashwin(7<sup>th</sup>day)<br>
        Diwali ends - Shukla Paksha Dwitiya (2<sup>nd</sup>day) of Kartik (8<sup>th</sup>month)
        </span>
        <p class="dpcontent">As per Purnimanta Calendar</p>
        <span class="dpred dpcontent">
        Diwali begins - Krishna Paksha Trayodashi (13<sup>th</sup>day) of Kartik (8<sup>th</sup>month)<br>
        Diwali ends - Shukla Paksha Dwitiya (17<sup>th</sup> day) of Kartik (8<sup>th</sup> month)
        </span>
        <p class="dpcontent">Diwali is celebrated as per luni-solar based Hindu calendar, its date(s) varies on Gregorian calendar and usually falls in mid-October and mid-November. Diwali Calendar lists all five days of Diwali festivities for 1000 years.</p>
