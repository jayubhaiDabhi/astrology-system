        <p class="dpcontent">The third day of Lakshmi Puja is the most important day of five days festivities and most of the times this day is referred as Diwali Puja itself. Apart from above five festivals, the most famous festivals for which Diwali is known, Diwali Calendar lists several other festivals which are celebrated during 5 days Diwali festivities.</p>
        <h2 class="dptitle">Diwali Observance</h2>
        <p class="dpcontent">Numerous rituals are followed during Diwali. These rituals vary from state to state and within a state region to region. However,</p>
        <ul class="dpcontent">
            <li class="dplist">cleaning and decorating homes usually by giving new whitewash or fresh paints,</li>
            <li class="dplist">buying new clothes and jewelry,</li>
            <li class="dplist">buying new household items either big or small,</li>
            <li class="dplist">preparing traditional home-made sweets,/li>          
            <li class="dplist">worshipping numerous deities,</li>          
            <li class="dplist">lighting Diya(s) and decorating home with blinking electric lamps,</li>          
            <li class="dplist">bursting firecrackers,</li>          
            <li class="dplist">trying Diwali remedies to gain wealth,</li>          
            <li class="dplist">visiting relatives and family friends,</li>          
            <li class="dplist">distributing sweets, dry-fruits and gifts,</li>          
            <li class="dplist">calling distant family members, relatives and friends to exchange Diwali wishes are the most common activities during Diwali.</li>          
        </ul>
        <h2 class="dptitle">Diwali Regional Variance</h2>
        <p class="dpcontent">Diwali celebrations are more extravagant in north Indian states. In South India like Holi, Diwali is a not as spectacular as that of North India. If one wants to enjoy spectacular fire-work at night then Delhi, Hyderabad and Mumbai are the most suitable metros to be in during Diwali.</p>
        <p class="dpcontent">Diwali celebrations are moderate in Chennai and Kolkata. In Chennai, Tamil Nadu, Narak Chaturdashi is more significant than Lakshmi Puja and in Kolkata, West Bengal, devotees worship Goddess Kali rather than Goddess Lakshmi on the third day of Diwali.</p>
        <h2 class="dptitle">Diwali Public Life</h2>
        <p class="dpcontent">Most of the public places function as normal during Diwali. On the eve of Diwali most restaurants, pubs, metro trains, buses, taxies, cinema halls and shops, emergency and critical services at hospitals function as normal. However, at most commercial places more than half of the staff would be on leave.</p>
        <p class="dpcontent">As most businessmen perform Chopda Puja and Lakshmi Puja on the day of Diwali, most shops and private offices are open due to this reason. Stock Exchanges in India, although being closed due to Diwali holiday, are opened only for an hour for Muhurat trading in the evening. Muhurat trading is a symbolic ritual which is considered auspicious among traders and getting performed for years now.</p>
        <p class="dpcontent">For most big and small businesses Diwali, like Christmas in western countries, is the peak season when maximum sales are record. Many Bollywood blockbusters are planned and released during Diwali.</p>
        <p class="dpcontent">However Diwali, being marked as Gazette Holidays, all government offices and closed. Most schools and colleges are closed during Diwali.</p>
        <p class="dpcontent">In recently years, in most metros, time limit has been imposed for displaying and bursting the firework to curtail the noise pollution and for those who want to sleep peacefully on the night of Diwali. At most places either 10 p.m. or 11 p.m. time limit is imposed for bursting the firework.</p>
        <h2 class="dptitle">Diwali in other Religions</h2>
        <p class="dpcontent">Diwali is historically a Hindu religion festival having its origin in the Era of Lord Rama or probably even before that. However, Diwali is also celebrated in Sikhism and Jainism but for separate reasons. Sikh celebrates Diwali as Bandi Chhor Divas and Jain celebrates it as a day to commemorate Mahavir.</p>
