            <h2 class="dptitle">About Sunrise and Sunset</h2>
            <p class="dpcontent">Panchang makers use either visibility of upper edge or visibility of middle limb of the Sun at eastern horizon to mark the sunrise time used in Panchang. The difference in both timing of Sunrise could be up to few minutes.</p>
            <p class="dpcontent">There is no consensus and most Panchang makers don't even mention it in their Panchang. Generally, upper edge timing of sunrise is considered as astronomical sunrise and middle limb timing of sunrise is considered as Hindu Sunrise. Most newspapers publish astronomical value of sunrise which consider upper edge of the Sun to mark the sunrise time.</p>
            <p class="dpcontent">Modern research shows that upper edge of Sunrise is more appropriate and authenticated to use as sunrise time in Panchang.</p>
            <h2 class="dptitle">As per Varahamira -</h2>
            <p class="dpcontent"  style="color:#a00; text-align:center;"><span>उदयास्त मनाख्यं हि दर्शनादर्शनं रवेः।</span></p>
            <p class="dpcontent">which means "visibility of sun's limb is the rising time and complete invisibility is the setting time."</p>
            <h2 class="dptitle">Authors of Dharmashastras also says -</h2>
            <p class="dpcontent"  style="color:#a00; text-align:center;"><span>रेस्वन्प्रभृत्यथादित्यात मुहूर्तन्त्रयमेवतु।</span></p>
            <p class="dpcontent">which means "the first three Muhurta of the day counted from the first visibility of the sun's rays." Further it mentions -</p>
            <p class="dpcontent"  style="color:#a00; text-align:center;"><span>रेखामात्रन्तु दृश्येत रश्मिभिश्च समन्वितं।<br> उदयन्तद्विजानीयात् होमं कूय्यात् विचक्षणः॥</span></p>
            <p class="dpcontent">which means "when even a portion of the sun with it's ray is visible it is to be considered as sunrise and at that time oblation (homa) is to be performed".</p>
            <p class="dpcontent">Drik Panchang shows both values side-by-side to clear the difference between upper edge and middle limb timing of sunrise and sunset. We also provide setting to change sunrise time between "Upper Edge" and "Middle Limb".</p>
            <p class="dpcontent">Drik Panchang uses upper edge of sun along with refraction to mark the time of sunrise. Further, by default we don't consider elevation for Sunrise calculations but provide option to enable elevation in Sunrise calculations. Elevation can change sunrise timings up to few minutes. Once setting of sunrise is updated, the new value would be used for all Panchang calculations including festivals and Muhurta timings.</p>
