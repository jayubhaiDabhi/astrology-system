            moving in a circular path when observed
            from earth. This circular-path is known as
            zodiac. If we divide zodiac in twelve equal
            parts, each part is called a zodiac sign.</p>
        <p class="dpcontent">These twelve signs are - Aries, Taurus,
            Gemini, Cancer, Leo, Virgo, Libra, Scorpio,
            Sagittarius, Capricorn, Aquarius and Pisces.
            t's very important to remember the order of
            Zodiac signs, because signs are shown using
            humbers in a birth-chart. For example, if 1
            s written, it means Aries or first sign. If 2 is
            written, it means Taurus or second sign and
            so on.</p>
        <p class="dpcontent">As per mathematics, a circle consists of 360
            degrees. Therefore, 12th part of 360 degrees,
            i.e. a sign, consists of 30 degrees. However,
            we need not focus on mathematical part
            as of now. Just note that each zodiac sign
            covers 30 degrees.</p>
        <p class="dpcontent">Every zodiac sign has a fixed lordship and
            we should also memorize it. Lord of a sign
            is also referred as owner sometimes. Let's
            have a look at lords of different signs.</p>
        <p class="dpcontent">Sun and Moon own only one zodiac sign.
            Rahu and Ketu do not own any of the signs.
            Rest of the planets, i.e. Mars, Mercury,
            Jupiter, Venus and Saturn, own two zodiac
            signs.</p>
        <p class="dpcontent">Mars owns first sign - Aries. Taurus is
