        <p class="dpcontent">The term Dussehra is more common in North Indian states and Karnataka while the term Vijayadashami is more popular in West Bengal. Dussehra is also spelled as Dasara and Dashahara (दशहरा).</p>
        <h2 class="dptitle">Dussehra & Vijayadashami Origin | Significance</h2>
        <p class="dpcontent">The term Dussehra refers to killing of 10 headed demon Ravana and because of that Dussehra literally means removal as well as take away of 10 sins. Some of the Dussehra rituals are intended to get rid of the ten human weaknesses as well as bad qualities from the person. These bad qualities, which are also attributed to 10 heads of demon Ravana, are as following -</p>
        <ol class="dpcontent">
            <li class="dplist">Kama Vasana - काम वासना (lust)</li>
            <li class="dplist">Krodha - क्रोध (anger)</li>
            <li class="dplist">Moha - मोह (attraction)</li>
            <li class="dplist">Lobha - लोभ (greed)</li>
            <li class="dplist">Mada - मद (over pride)</li>
            <li class="dplist">Matsara - मत्सर (jealousy)</li>
            <li class="dplist">Swartha - स्वार्थ (selfishness)</li>
            <li class="dplist">Anyaya- अन्याय (injustice)</li>
            <li class="dplist">Amanavata - अमानवता (cruelty)</li>
            <li class="dplist">Ahankara - अहङ्कार (ego)</li>
        </ol>
        <p class="dpcontent">In Hindu calendar four Dashami are very significant. Dashami Tithi during Ashwin month is one of them and it is known as Vijayadashami i.e. the tenth day which bestows victory. It is highly likely that the day got its name due to making Lord Rama victorious over mighty demon Ravana. It is important to mention that the killing of 10 headed demon Ravana on the 10th day of Ashwin month might be more than a coincidence.</p>
        <h2 class="dptitle">Dussehra & Vijayadashami Deity(s)</h2>
        <p class="dpcontent">Lord Rama - The most popular deity of Dussehra is Lord Rama. The day is observed as the victory of Lord Rama over demon Ravana. It is also important to mention that Lord Rama took 20 lunar days to return to Ayodhya after killing Ravana. It was Diwali day when Lord Rama reached Ayodhya. Hence the day of Diwali is also celebrated as the coming back of Lord Rama to Ayodhya after completing the exile of fourteen years.</p>
        <p class="dpcontent">Goddess Aparajita - In many regions, Goddess Aparajita is worshipped on the day of Dussehra and Vijayadashami. As the name suggest, Goddess Aparajita cannot be defeated. As per religious legends, Lord Rama sought blessings of Goddess Aparajita before waging war against Ravana. However, in Vedic time the worship of goddess Aparajita was restricted to Kshatriyas and kings.</p>
        <p class="dpcontent">Shami Tree - Worshipping Shami tree on the day of Vijayadashami is very significant. It is believed that Arjun hid his weapons inside Shami tree during his exile. Shami Puja is also known as Banni Puja and Jammi puja in some of the southern states in India.</p>
        <h2 class="dpcontent">Dussehra & Vijayadashami Date and Time</h2>
        <p class="dpcontent">Dussehra and Vijayadashami are observed on Shukla Paksha Dashami during Ashwin lunar month. If Dashami Tithi overlaps on two consecutive days in Gregorian calendar then Shravana Nakshatra plays an important role in deciding the day of Vijayadashami.</p>
