        <h2 class="dptitle">Dussehra & Vijayadashami Public Life</h2>
        <p class="dpcontent">Dussehra and Vijayadashami are compulsory gazetted holidays in India. During a compulsory gazetted holiday, all government offices across India are closed. Dussehra is also a bank holiday. Hence all public and private banks are also closed on the day of Vijayadashami.</p>
        <p class="dpcontent">Apart from government offices and banks, most businesses, schools and colleges are closed on the day of Dussehra. However, public transport runs as usual on the day of Dussehra.</p>
